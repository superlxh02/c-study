#include "calc.h"              /* 包含自己的头文件 */

/* -------------------------------------------------------
 *  计算平方：返回 x * x
 * -------------------------------------------------------*/
int square(int x)
{
    return x * x;
}

/* -------------------------------------------------------
 *  计算立方：返回 x * x * x
 * -------------------------------------------------------*/
int cube(int x)
{
    return x * x * x;
}
