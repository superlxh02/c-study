/* ====================================================================
 *  一、为什么需要头文件？
 * ====================================================================
 *
 * 当所有代码写在一个 .c 文件中时，遇到的问题：
 *   - 文件太长，查找函数定义困难
 *   - 代码无法复用（想用某个函数就得复制粘贴）
 *   - 多人合作时频繁冲突
 *   - 每次改一行都要重新编译整个文件
 *
 * 解决方案：把代码拆分成多个文件。
 *   头文件（.h） → 存放"声明"（函数头 + 分号）
 *   源文件（.c） → 存放"定义"（函数头 + 函数体）
 *
 * 类比：头文件 = 说明书的目录页，源文件 = 说明书的具体内容。
 */

/* ====================================================================
 *  二、函数声明 vs 函数定义
 * ====================================================================
 *
 * 函数声明（Declaration）：
 *   int add(int a, int b);          ← 只告诉编译器参数类型和返回值类型
 *
 * 函数定义（Definition）：
 *   int add(int a, int b)           ← 包含函数的完整实现
 *   {
 *       return a + b;
 *   }
 *
 * 函数声明的作用：告诉编译器"别急，这个函数在后面（或其他文件里）有定义"。
 * 编译器只需要声明就能生成正确的调用代码，真正的"合体"由链接器完成。
 */

/* ====================================================================
 *  三、头文件的包含保护
 * ====================================================================
 *
 * 问题：头文件可能被多次包含，导致"类型重复定义"错误。
 * 例如：a.h 被 b.h 包含，main.c 又同时包含了 a.h 和 b.h。
 *
 * 解法一：经典写法（C 标准方式）
 *   #ifndef MATHUTILS_H      ← 如果宏 MATHUTILS_H 未定义
 *   #define MATHUTILS_H      ← 定义这个宏
 *   // ... 头文件内容 ...
 *   #endif                   ← 结束条件编译块
 *
 *   第一次包含：宏未定义 → 通过检查 → 定义宏 → 内容生效
 *   第二次包含：宏已定义 → 检查失败 → 内容被跳过
 *
 * 解法二：#pragma once（更简洁，大多数编译器支持）
 *   #pragma once
 *   // ... 头文件内容 ...
 *
 *   编译器指令："本文件在一个编译单元中只包含一次。"
 */

/* ====================================================================
 *  四、多文件编译流程
 * ====================================================================
 *
 * 以本章的 mathutils 项目为例，目录结构：
 *   ch16-headers/
 *   ├── include/
 *   │    └── mathutils.h       ← 头文件：声明 add()、subtract()
 *   └── src/
 *        ├── main.c            ← 主程序：调用 add()、subtract()
 *        └── mathutils.c       ← 实现文件：add()、subtract() 的代码
 *
 * 编译命令（在 ch16-headers/ 目录下执行）：
 *   clang src/main.c src/mathutils.c -Iinclude -o mathutils_demo.exe
 *
 *   参数说明：
 *     src/main.c          → 第一个源文件
 *     src/mathutils.c     → 第二个源文件
 *     -Iinclude           → 让编译器去 include/ 目录找头文件
 *                          （等价于 clang -Iinclude ...)
 *     -o program.exe      → 指定输出文件名
 *
 * 编译流程分解：
 *   1. 预处理（Preprocessing）
 *      - 展开 #include，把头文件内容插入源文件
 *      - 处理 #define 宏
 *      - 生成 .i 中间文件
 *
 *   2. 编译（Compilation）
 *      - 将预处理后的代码翻译成汇编代码
 *      - 生成 .s 文件
 *
 *   3. 汇编（Assembly）
 *      - 将汇编代码翻译成机器码
 *      - 生成 .o（目标文件）
 *
 *   4. 链接（Linking）
 *      - 将 main.o 和 mathutils.o 合并
 *      - 解析函数调用地址
 *      - 生成最终的可执行文件 .exe
 *
 *   关键点：main.c 中调用 add() 时，编译器只看到声明（来自 mathutils.h），
 *   不知道 add() 的地址。编译器在目标文件中留下一个"空位"，
 *   链接器在合并所有 .o 文件时填入正确的地址。
 */

/* ====================================================================
 *  五、目录组织规范
 * ====================================================================
 *
 * 推荐的 C 项目目录结构：
 *   项目名/
 *   ├── include/      ← 头文件（.h）：对外暴露的接口
 *   │    ├── mathutils.h
 *   │    └── calc.h
 *   └── src/          ← 源文件（.c）：具体实现
 *        ├── main.c
 *        ├── mathutils.c
 *        └── calc.c
 *
 * 头文件应该放什么：
 *   - 函数声明（函数头 + 分号）
 *   - 结构体、共用体、枚举的类型定义
 *   - 宏定义（#define）
 *   - 全局变量的 extern 声明
 *
 * 源文件应该放什么：
 *   - 函数的完整定义（函数体）
 *   - 只在当前文件使用的辅助函数和变量
 *   - #include 对应的头文件
 *
 * 核心规则：
 *   1. 头文件放声明，源文件放定义
 *   2. 自己的头文件用双引号 #include "xxx.h"
 *   3. 系统头文件用尖括号 #include <xxx.h>
 *   4. 头文件必须加包含保护（防止重复定义）
 *   5. 编译时用 -I 参数指定头文件路径
 *   6. 多个 .c 文件一起编译，链接器自动合并
 */

/* ====================================================================
 *  六、void main(void) vs int main(void)
 * ====================================================================
 *
 * 某些旧教程会写 void main(void)，但宿主环境中的标准 C 写法应返回 int。
 * main 返回 0 表示正常退出，非零值通常表示程序失败。
 * 推荐写法：
 *   int main(void)       ← 不接收命令行参数时的标准写法
 *   {
 *       // ...
 *       return 0;        ← 返回 0 告诉操作系统"程序正常结束"
 *   }
 */

