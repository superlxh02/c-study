/**
 * @file    01_union_demo.c
 * @chapter 第十三章 - 共用体与枚举
 * @section 一.1.3 - 共用体语法示例
 * @brief   演示共用体（union）的核心特性：所有成员共享同一块内存，
 *          给一个成员赋值会覆盖其他成员的值。
 *
 * 核心知识点：
 *   1. union 的语法和 struct 相似，但成员共享内存
 *   2. 同一时刻只有一个成员的值是有效的
 *   3. 给新成员赋值后，应只把最后写入的成员视为当前有效成员
 *   4. 访问成员同样使用 . 运算符
 */

#include <stdio.h>

/* 定义共用体 Data
 * i (int)、f (float)、c (char) 共享同一块内存空间
 * 大小至少能容纳最大的成员（通常为 4 字节） */
union Data
{
    int i;      /* 整型，通常占 4 字节 */
    float f;    /* 浮点型，通常占 4 字节 */
    char c;     /* 字符型，占 1 字节 */
};

int main(void)
{
    union Data d;   /* 声明共用体变量 */

    /*--------------------------------------------------------------------
     * 步骤1：给成员 i 赋值，然后输出
     * 此时 d 的内存中存放的是整数 100 的二进制表示
     *--------------------------------------------------------------------*/
    d.i = 100;
    printf("d.i = %d\n", d.i);   /* 输出 100 —— 正常 */

    /*--------------------------------------------------------------------
     * 步骤2：给成员 f 赋值 3.14
     * 此时 d 的同一块内存被覆盖为浮点数 3.14 的二进制表示
     * 此后不要再读取 d.i；最后写入的成员才是当前有效成员
     *--------------------------------------------------------------------*/
    d.f = 3.14f;
    printf("d.f = %f\n", d.f);   /* 输出 3.140000 */
    /* 注意：此时 d.i 的值已经无效了，下面不要再读取 d.i */

    /*--------------------------------------------------------------------
     * 步骤3：给成员 c 赋值 'A'
     * 内存再次被覆盖为字符 'A' 的 ASCII 码 (65)
     * 此时 d.f 和 d.i 都不再是当前有效成员
     *--------------------------------------------------------------------*/
    d.c = 'A';
    printf("d.c = %c\n", d.c);   /* 输出 A —— 正常 */
    /* 同理，写入 d.c 后，下面不要再读取 d.f 或 d.i */

    printf("\n结论：union 所有成员共享同一块内存，同一时刻只有一个有效。\n");
    printf("写入新成员后，应只读取最后写入的那个成员。\n");

    return 0;
}
