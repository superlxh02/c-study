/* ==== 头文件 ==== */
#include <stdio.h>
#include <stddef.h>  /* NULL */

/* =================================================================
 * 【片段1】指针变量声明（对应 第七章 一-2.3）
 * 语法：数据类型 *指针变量名;
 * ================================================================= */
void demo_declaration(void)
{
    printf("====== 片段1：指针变量声明 ======\n");

    /* 声明不同类型的指针变量 */
    int   *p;       /* p 是 int 的指针，专门存 int 类型变量的地址 */
    float *q;       /* q 是 float 的指针，专门存 float 类型变量的地址 */
    char  *r;       /* r 是 char 的指针，专门存 char 类型变量的地址 */

    /* 方义：声明后的指针尚未初始化，此时为野指针，不可解引用！
     * 本例暂不对它们解引用，仅演示声明语法。 */

    /* 初始化并使用 */
    int a = 42;
    float b = 3.14f;
    char c = 'Z';

    p = &a;
    q = &b;
    r = &c;

    printf("int   指针 p 指向的值：%d\n", *p);
    printf("float 指针 q 指向的值：%.2f\n", *q);
    printf("char  指针 r 指向的值：%c\n", *r);
    /* 预期输出：
       int   指针 p 指向的值：42
       float 指针 q 指向的值：3.14
       char  指针 r 指向的值：Z
    */

    printf("\n");
}

/* =================================================================
 * 【片段2】取地址 & 与赋值（对应 第七章 一-3）
 * 两步：① &变量名 获取地址  ② 赋给指针变量
 * ================================================================= */
void demo_address_and_assign(void)
{
    printf("====== 片段2：取地址 & 与赋值 ======\n");

    int a = 100;         /* 声明普通变量 a，值为 100 */
    int *p;              /* 声明指针变量 p               */

    p = &a;              /* 把 a 的地址赋值给 p（p 现在"指向"了 a）*/
    printf("a = %d, *p = %d\n", a, *p);
    /* 预期输出：a = 100, *p = 100 */

    /* 也可以一步完成：声明并初始化 */
    int b = 200;
    int *q = &b;         /* 声明同时指向 b */
    printf("b = %d, *q = %d\n", b, *q);
    /* 预期输出：b = 200, *q = 200 */

    /* 注意：声明时的 * 是类型标记，赋值时不需要 * */
    int c = 300;
    int *r;              /* 这里的 * 是"声明语法的一部分"，表示 r 是指针 */
    r = &c;              /* 赋值时 r 前面没有 *，就是把地址赋给 r 本身  */

    printf("c = %d, *r = %d\n", c, *r);
    /* 预期输出：c = 300, *r = 300 */

    printf("\n");
}

/* =================================================================
 * 【片段3】* 的两种角色（对应 第七章 二-1.4）
 * ① 声明中的 *：类型标记，"p 是指针类型"
 * ② 执行语句中的 *：解引用运算符，"p 指向的那个变量"
 * ================================================================= */
void demo_star_two_roles(void)
{
    printf("====== 片段3：* 的两种角色对比 ======\n");

    int a = 100;
    int *p;       /* ① 声明语句中的 *：表示"p 是指针类型" */
    p = &a;       /* 把 a 的地址赋给 p */

    printf("a 的初始值：%d\n", a);

    *p = 200;     /* ② 执行语句中的 *：解引用，"p 指向的那个变量"即 a */
    printf("执行 *p = 200 后，a = %d\n", a);
    /* 预期输出：执行 *p = 200 后，a = 200 */

    /* 核心记忆：
     *   声明里的 * → "招牌"（告诉编译器类型）
     *   执行里的 * → "开门"（顺着地址找到盒子）
     */

    printf("\n");
}

/* =================================================================
 * 【片段4】指针自己的地址（对应 第七章 二-2.1）
 * 指针和被指向的变量是两个独立的东西，各自有地址
 * ================================================================= */
void demo_pointer_own_address(void)
{
    printf("====== 片段4：指针自己的地址 ======\n");

    int a = 100;
    int *p = &a;

    /* p 和 a 是两个不同的变量，有各自的地址 */
    printf("a 的地址：%p\n", (void *)&a);
    printf("p 的地址：%p\n", (void *)&p);  /* p 自己也有地址！ */
    /* p 的值 等于 a 的地址 */
    printf("p 的值（即 a 的地址）：%p\n", (void *)p);

    /* 预期输出（具体地址因运行环境而异，但关系固定）：
       a 的地址：000000BFCB5FFBBC    (示例)
       p 的地址：000000BFCB5FFBB0    (不同于 a)
       p 的值（即 a 的地址）：000000BFCB5FFBBC  (等于 a 的地址)
    */

    printf("\n");
}

/* =================================================================
 * 【片段5】一个指针先后指向不同变量（对应 第七章 二-2.2）
 * 指针变量也是变量，其值（地址）可以修改
 * ================================================================= */
void demo_pointer_reassign(void)
{
    printf("====== 片段5：一个指针指向不同变量 ======\n");

    int a = 10, b = 20;
    int *p;

    p = &a;
    printf("p 指向 a 时，*p = %d\n", *p);   /* 10 */

    p = &b;                                  /* p 改为指向 b */
    printf("p 改为指向 b 时，*p = %d\n", *p); /* 20 */

    /* 预期输出：
       p 指向 a 时，*p = 10
       p 改为指向 b 时，*p = 20
    */

    printf("\n");
}

/* =================================================================
 * 【片段6】多个指针指向同一个变量（对应 第七章 二-2.3）
 * 通过任意一个指针修改，另一个也能看到变化
 * ================================================================= */
void demo_multiple_pointers(void)
{
    printf("====== 片段6：多个指针指向同一变量 ======\n");

    int a = 100;
    int *p1 = &a;
    int *p2 = &a;

    printf("修改前：a = %d, *p1 = %d, *p2 = %d\n", a, *p1, *p2);

    *p1 = 200;               /* 通过 p1 修改 a */
    printf("通过 *p1 = 200 修改后：a = %d, *p1 = %d, *p2 = %d\n", a, *p1, *p2);
    /* 预期输出：
       修改前：a = 100, *p1 = 100, *p2 = 100
       通过 *p1 = 200 修改后：a = 200, *p1 = 200, *p2 = 200
       说明：p1 和 p2 指向同一个 a，通过任一修改，另一个也"看到"变化
    */

    printf("\n");
}

/* =================================================================
 * 【片段7】空指针 NULL（对应 第七章 三-1.1）
 * 暂时不指向任何变量时，初始化为 NULL
 * ================================================================= */
void demo_null_pointer(void)
{
    printf("====== 片段7：空指针 NULL ======\n");

    int *p = NULL;           /* p 现在不指向任何变量 */

    /* 解引用前检查是否为 NULL，这是安全编程的好习惯 */
    if (p != NULL)
    {
        *p = 100;            /* 安全：只有不空时才解引用 */
    }
    else
    {
        printf("p 为 NULL，不能解引用，跳过写入操作。\n");
    }
    /* 预期输出：p 为 NULL，不能解引用，跳过写入操作。 */

    /* 好的初始化习惯 */
    int *q = NULL;           /* 明确表示现在不指向任何地方 */
    (void)q;                 /* 本片段只强调初始化，不对空指针做任何访问。 */
    printf("q 被初始化为 NULL。\n");

    printf("\n");
}

/* =================================================================
 * 【片段8】野指针的危险（对应 第七章 三-1.2）
 * 声明了指针但没有初始化 → 内部是随机垃圾地址 → 解引用极度危险
 *
 * !! 警告：本函数中的野指针演示代码已被注释保护！
 *    取消注释可能导致程序崩溃或不可预测行为。
 *    这里仅展示"什么是野指针"的概念。
 * ================================================================= */
void demo_wild_pointer(void)
{
    printf("====== 片段8：野指针的危险（仅概念演示） ======\n");

    printf("野指针：声明了指针但没有初始化的指针。\n");
    printf("它里面的值是一个随机的垃圾地址。\n\n");

    /*
     * 以下代码极端危险，切勿在实际代码中这样做：
     *
     *   int *p;        // 声明了指针但没有初始化
     *   *p = 100;      // 危险！p 未初始化，读取并解引用会产生未定义行为
     *
     * 比喻：你随手拿了一张便签，上面原来就写着某个门牌号，
     * 可能是"总经理保险柜 888 号"。你顺着这个地址去存东西，
     * 等于把数据写到了完全不该碰的地方——可能修改系统关键数据。
     */

    printf("正确做法：声明指针时立即初始化\n");
    printf("  int *p = NULL;    // 暂时不指向任何地方\n");
    printf("  int *p = &var;    // 指向一个已有变量\n");

    printf("\n");
}

/* =================================================================
 * 【片段9】二级指针 / 指向指针的指针（对应 第七章 三-2）
 * 指针变量本身也有地址 → 可以用另一个指针指向它
 * ================================================================= */
void demo_double_pointer(void)
{
    printf("====== 片段9：二级指针 ======\n");

    int a = 100;
    int  *p  = &a;       /* p 指向 a（一级指针）        */
    int **pp = &p;       /* pp 指向 p（二级指针）       */

    /* 三种访问方式 */
    printf("直接访问 a    ：%d\n", a);       /* 100 */
    printf("一次解引用 *p ：%d\n", *p);      /* 100（通过 p 找到 a） */
    printf("两次解引用 **pp：%d\n", **pp);   /* 100（先找到 p，再找到 a） */

    /* 预期输出：
       直接访问 a    ：100
       一次解引用 *p ：100
       两次解引用 **pp：100
    */

    /* 各表达式的含义（对照总结表）：
     *  表达式          含义                         结果类型
     *  ────────       ──────                       ──────
     *  a              普通变量 a 的值              int
     *  p              p 本身的值（即 a 的地址）     int*（地址）
     *  *p             p 指向的变量的值（即 a 的值） int
     *  &a             a 的地址                     int*（地址）
     *  &p             p 自己的地址                  int**（指针的地址）
     *  pp             pp 的值（即 p 的地址）        int**
     *  *pp            pp 指向的值（即 p 的值）      int*
     *  **pp           等价于 *p，即 a 的值          int
     */

    printf("\n");
}

/* =================================================================
 * main — 依次调用各片段演示函数
 * ================================================================= */
int main(void)
{
    demo_declaration();
    demo_address_and_assign();
    demo_star_two_roles();
    demo_pointer_own_address();
    demo_pointer_reassign();
    demo_multiple_pointers();
    demo_null_pointer();
    demo_wild_pointer();
    demo_double_pointer();

    return 0;
}
