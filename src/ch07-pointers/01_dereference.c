/**
 * =====================================================
 * 文件名: 01_dereference.c
 * 所属章节: 第七章
 * 对应小节: 二-1 解引用运算符 *
 * 演示内容: 通过指针解引用读取变量值，以及通过指针修改变量值
 * =====================================================
 *
 * 核心知识点：
 *   - *指针变量名  获取指针所指向的变量的值（读）
 *   - *指针变量名 = 新值  修改指针所指向的变量的值（写）
 *   - *p 等价于 p 指向的那个变量
 *
 * 编译：gcc -Wall -o 01_dereference 01_dereference.c
 * 运行：./01_dereference
 */

#include <stdio.h>

int main(void)
{
    /* 步骤1：声明普通变量 a 和指针变量 p，让 p 指向 a */
    int a = 100;
    int *p = &a;    /* p 存储的是 a 的内存地址 */

    /* 步骤2：通过解引用运算符 * 读取 p 所指向变量的值
     *   *p 的含义："顺着 p 里存的地址，找到那个盒子，把里面的内容拿出来"
     */
    printf("a 的值：%d\n", a);         /* 100 —— 直接访问变量名 */
    printf("*p 的值：%d\n", *p);       /* 100 —— 通过指针间接访问   */

    /* 步骤3：通过解引用运算符 * 修改 p 所指向变量的值
     *   *p = 200 的含义："顺着 p 里存的地址找到那个变量，把它的值改成 200"
     *   因为 p 指向的是 a，所以实际上就是把 a 改成了 200
     */
    *p = 200;

    /* 步骤4：验证修改结果 —— 两种访问方式都能看到变化 */
    printf("--- 通过 *p 修改后 ---\n");
    printf("a 的值：%d\n", a);         /* 200 —— 直接访问变了       */
    printf("*p 的值：%d\n", *p);       /* 200 —— 通过指针访问也变了 */

    return 0;
}

