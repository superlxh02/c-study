#include <stdio.h>

int main(void)
{
    /* ============================================================
     * 练习1：基本的指针操作
     *   - 声明指针并指向变量
     *   - 通过指针读取变量的值
     *   - 通过指针修改变量的值
     * ============================================================ */
    int x = 42;
    int *p = &x;               /* p 指向 x */

    printf("=== 练习1：基本操作 ===\n");
    printf("x 的值：%d\n", x);
    printf("p 的值（x 的地址）：%p\n", (void *)p);
    printf("*p 的值：%d\n", *p);

    /* 通过指针修改 x 的值 */
    *p = 99;
    printf("\n通过 *p = 99 修改后：\n");
    printf("x 的值：%d\n", x);
    printf("*p 的值：%d\n\n", *p);

    /* ============================================================
     * 练习2：指针自己的地址
     *   指针变量本身也是变量，有自己的内存地址。
     *   &p 得到的是 p 自己的地址，这和 p 里面存的值（a 的地址）
     *   是完全不同的两回事。
     * ============================================================ */
    printf("=== 练习2：指针自己的地址 ===\n");
    printf("x 的地址：%p\n", (void *)&x);     /* x 在内存中的位置 */
    printf("p 的地址：%p\n", (void *)&p);     /* p 自身在内存中的位置 */
    printf("p 自己的地址和它存的值是不同的！\n\n");

    /* ============================================================
     * 练习3：一个指针先后指向不同变量
     *   指针变量也是变量，它里面的值（地址）可以修改。
     *   这跟普通变量可以重新赋值是一样的道理。
     * ============================================================ */
    int a = 10, b = 20;
    int *ptr;                   /* 先声明指针，暂不初始化 */

    printf("=== 练习3：改变指针的指向 ===\n");
    ptr = &a;                   /* ptr 指向 a */
    printf("ptr 指向 a 时，*ptr = %d\n", *ptr);

    ptr = &b;                   /* ptr 改为指向 b */
    printf("ptr 指向 b 时，*ptr = %d\n\n", *ptr);

    /* ============================================================
     * 练习4：二级指针简单演示
     *   既然指针变量本身也有地址，那能不能用一个指针来指向
     *   另一个指针呢？当然可以，这就是二级指针。
     *
     *   层级关系：
     *     num (int)  ←  p1 (int *)  ←  p2 (int **)
     *     值:5          值:&num        值:&p1
     * ============================================================ */
    int num = 5;
    int *p1 = &num;             /* 一级指针：指向 int */
    int **p2 = &p1;             /* 二级指针：指向 int* */

    printf("=== 练习4：二级指针 ===\n");
    printf("num = %d\n", num);       /* 直接访问原始变量 */
    printf("*p1 = %d\n", *p1);       /* 一次解引用：p1→num */
    printf("**p2 = %d\n", **p2);     /* 两次解引用：p2→p1→num */

    return 0;
}

