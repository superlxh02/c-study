/* #include 是预处理指令，在编译前把 stdio.h 的内容复制进来
 * stdio.h 是标准输入输出头文件，提供 printf 等函数的声明
 * 预处理指令以 # 开头，不需要分号结尾 */
#include <stdio.h>

/* int main(void) 是不接收参数的主函数定义
 * 每一个普通的 C 可执行程序必须有且只有一个 main 函数
 * 程序运行时从 main 的第一条语句开始执行，逐行向下 */
int main(void)
{
    /* printf 是标准库提供的输出函数，用于向屏幕打印文字
     * "Hello, World!\n" 是要输出的字符串：
     *   - Hello, World! 是普通字符，原样输出
     *   - \n 是转义字符，表示换行（光标移到下一行开头）
     * 语句必须以分号 ; 结尾，告诉编译器"这句话到此结束" */
    printf("Hello, World!\n");

    /* return 0; 表示程序正常结束，把值 0 返回给操作系统
     * 返回值 0 一般表示程序执行成功，非 0 表示出错
     * 这也是 main 函数中唯一可以省略的 return（C99 起），但建议始终写上 */
    return 0;
}

