#include <stdio.h>

int main(void)
{
    /* ====================================================
     * 练习1：计算 1 到 100 的和（使用 for 循环）
     * 累加模式：sum = sum + i，每次循环把当前数字加到总和中
     * ==================================================== */
    int sum = 0;                         /* sum 存储累加和，初始化为 0 */
    for (int i = 1; i <= 100; i++)
    {
        sum = sum + i;                   /* 等价写法：sum += i; */
    }
    printf("1 到 100 的和是：%d\n", sum);

    /* ====================================================
     * 练习2：打印 1 到 20 中的偶数（使用 for 循环 + 步长控制）
     * i 从 2 开始，每次增加 2，这样 i 始终是偶数
     * i = i + 2 也可以简写为 i += 2
     * ==================================================== */
    printf("1 到 20 的偶数：");
    for (int i = 2; i <= 20; i = i + 2)  /* 步长为 2，只遍历偶数 */
    {
        printf("%d ", i);                /* 每次输出后加一个空格，数字不换行 */
    }
    printf("\n");                        /* 最后输出一个换行符，格式整理 */

    /* ====================================================
     * 练习3：用 while 实现倒计时（从 5 数到 1，然后说"发射"）
     * count-- 是 count = count - 1 的简写形式
     * ==================================================== */
    int count = 5;                       /* 倒计时从 5 开始 */
    while (count > 0)                     /* 当 count 大于 0 时继续 */
    {
        printf("倒计时：%d\n", count);
        count--;                         /* 等价于 count = count - 1，每次减 1 */
    }
    printf("发射！\n");                   /* 倒计时结束 */

    return 0;
}

