/**
 * =====================================================
 * 文件名: 24_scanf_demo.c
 * 所属章节: 第一章 - C语言核心基础语法
 * 对应小节: 九、2. 输入：scanf 函数
 * 演示内容: 使用 scanf 从键盘读取用户输入的数据
 *           读取整数（%d）、小数（%f）、字符（%c）
 *           关键注意事项：& 取地址符、%c 前的空格
 * =====================================================
 */

#include <stdio.h>

int main(void)
{
    /* 声明变量用于存储用户输入的数据
     * 注意：这些变量没有赋初始值，它们的值将由 scanf 填入 */
    int age;          /* 存储用户输入的年龄（整数） */
    float height;     /* 存储用户输入的身高（小数） */
    char grade;       /* 存储用户输入的成绩等级（字符） */

    /* ---- 读取整数 ----
     * scanf 格式："%d" 表示读取一个十进制整数
     * &age 表示把读取到的值存入 age 变量的地址中
     * 必须写 & 符号！（& 是"取地址"运算符）
     * 程序会暂停，等待用户在键盘上输入数字并按回车 */
    printf("请输入你的年龄：");
    if (scanf("%d", &age) != 1)
    {
        fprintf(stderr, "年龄必须是整数。\n");
        return 1;
    }

    /* ---- 读取小数 ----
     * %f 表示读取一个 float 类型的浮点数
     * 同样需要 & 符号 */
    printf("请输入你的身高（米）：");
    if (scanf("%f", &height) != 1)
    {
        fprintf(stderr, "身高必须是有效小数。\n");
        return 1;
    }

    /* ---- 读取字符 ----
     * %c 表示读取一个字符
     * 注意 %c 前面的空格！这个空格用来跳过之前输入
     * 留下的回车符（\n）或空白字符
     * 如果不加空格，scanf 会把上一次输入的换行符当成字符读进去 */
    printf("请输入你的成绩等级（A/B/C/D）：");
    if (scanf(" %c", &grade) != 1)
    {
        fprintf(stderr, "没有读到成绩等级。\n");
        return 1;
    }

    /* ---- 输出用户输入的内容 ----
     * printf 验证 scanf 是否成功读取了数据
     * %.2f 控制身高显示两位小数 */
    printf("\n--- 你的信息 ---\n");
    printf("年龄：%d 岁\n", age);
    printf("身高：%.2f 米\n", height);
    printf("成绩等级：%c\n", grade);

    return 0;
}

/* ============================================================
 * 编译: clang scanf_demo.c -o scanf_demo.exe
 * 运行: scanf_demo.exe
 *
 * 运行示例（用户输入用 ← 表示回车）：
 *   请输入你的年龄：18
 *   请输入你的身高（米）：1.75
 *   请输入你的成绩等级（A/B/C/D）：A
 *
 *   --- 你的信息 ---
 *   年龄：18 岁
 *   身高：1.75 米
 *   成绩等级：A
 *
 * scanf 使用的注意事项：
 *   1. 变量名前必须加 &（取地址符），字符串数组除外
 *      scanf("%d", &x);    // 正确
 *      scanf("%d", x);     // 错误！会导致未定义行为
 *
 *   2. %c 前加空格跳过之前的换行符
 *      scanf(" %c", &ch);   // 正确
 *      scanf("%c", &ch);    // 可能读到换行符
 *
 *   3. 不要在 scanf 格式字符串中写提示信息
 *      printf("请输入：");  // 提示用 printf
 *      scanf("%d", &x);     // 读取用 scanf
 *
 *   4. scanf 的返回值表示成功读取了几个值
 *      可用于判断输入是否有效（见 26_guessing_game.c）
 * ============================================================
 */
