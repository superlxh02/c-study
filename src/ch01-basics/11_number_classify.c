#include <stdio.h>

int main(void)
{
    /* 设定要判断的数字 */
    int number = 15;

    /* ---- 练习1：判断一个数是正数、负数还是零 ----
     * 使用 if-else if-else 三路分支结构
     * 注意判断顺序：>0, <0, ==0（else 兜底） */
    if (number > 0)
    {
        /* number > 0 成立 → 正数 */
        printf("%d 是正数\n", number);
    }
    else if (number < 0)
    {
        /* number > 0 不成立但 number < 0 成立 → 负数 */
        printf("%d 是负数\n", number);
    }
    else
    {
        /* 以上条件都不成立，即 number == 0 */
        printf("这是零\n");
    }

    /* ---- 练习2：判断奇偶性 ----
     * 使用 % 取余运算符：number % 2 得到 number 除以 2 的余数
     * 余数为 0 表示能被 2 整除 → 偶数
     * 余数为 1 表示不能被 2 整除 → 奇数
     * == 是比较运算符，判断是否等于 0 */
    if (number % 2 == 0)
    {
        printf("%d 是偶数\n", number);
    }
    else
    {
        printf("%d 是奇数\n", number);
    }

    return 0;
}

