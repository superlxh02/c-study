/* 包含标准输入输出头文件，提供 printf 函数 */
#include <stdio.h>

/* 主函数：程序的入口 */
int main(void)
{
    /* 第一条 printf：输出第一行中文文字
     * \n 表示换行，输出完这行后光标移到下一行开头 */
    printf("这是我的第一个C程序\n");

    /* 第二条 printf：输出第二行文字
     * 每条 printf 从上一行的末尾（或行首）继续输出
     * 因为上一行末尾有 \n，所以这行会从新的一行开始 */
    printf("我可以写出多行文字\n");

    /* return 0 表示程序正常结束 */
    return 0;
}

