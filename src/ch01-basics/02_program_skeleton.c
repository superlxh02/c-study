/* 第1行：预处理指令
 * #include 在编译之前执行，把 stdio.h 的内容"复制粘贴"到这里
 * 它是对编译器发出的指令，不是程序运行时要执行的语句，所以不用分号 */
#include <stdio.h>

/* 第2行：主函数头部
 * int    —— 返回值类型，表示这个函数返回一个整数
 * main   —— 函数名，是程序的入口点
 * ()     —— 参数列表，这里为空（C语言中空括号表示参数未指定）
 * 注意：函数头不是一条独立的"语句"，而是函数定义的一部分，所以不加分号 */
int main(void)
/* 第3行：左花括号 —— 函数体的开始
 * 花括号成对出现，标记代码的范围（包裹），
 * 左花括号是开口，右花括号是封口，中间是函数要执行的语句 */
{
    /* 第4行：printf 语句（以分号结尾）
     * 调用标准库函数 printf 输出字符串到屏幕
     * \n 是转义字符，表示换行 */
    printf("Hello, World!\n");

    /* 第5行：return 语句（以分号结尾）
     * 结束 main 函数并返回 0 给操作系统，表示程序正常退出 */
    return 0;

/* 第6行：右花括号 —— 函数体的结束 */
}

/* ============================================================
 * C语言程序基本骨架的三个核心要点：
 *
 * 1. main 函数是程序的唯一入口
 *    - 程序从 main 的第一条语句开始执行
 *    - 普通的可执行程序必须有且只有一个 main 函数
 *
 * 2. 花括号 { } 标记代码的"范围"
 *    - 总是成对出现，标记一段代码的开始和结束
 *
 * 3. 分号 ; 是语句的结束标志
 *    - 绝大多数语句必须以分号结尾
 *    - 预处理指令（#开头）、函数头、花括号不需要分号
 *
 * 编译: clang program.c -o program.exe
 * 运行: program.exe
 *
 * 预期输出:
 *   Hello, World!
 * ============================================================
 */
