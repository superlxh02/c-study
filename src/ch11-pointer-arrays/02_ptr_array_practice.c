/**
 * @file    02_ptr_array_practice.c
 * @chapter 第十一章 - 指针和数组的关系
 * @section 三.3 - 动手练习（指针数组综合）
 * @brief   综合练习：三种遍历方式、sizeof 区别、指针数组与数组指针演示。
 *
 * 练习内容：
 *   练习1 - 用三种不同方式（下标法、指针偏移法、移动指针法）遍历数组
 *   练习2 - 验证 sizeof(数组名) 与 sizeof(指针变量) 的区别
 *   练习3 - 指针数组（存指针的数组）和数组指针（指向数组的指针）的简单演示
 *
 * 附加挑战（在注释中给出了实现提示）：
 *   挑战1 - 数组逆序（下标法 + 指针法）
 *   挑战2 - 用指针遍历二维数组所有元素
 *   挑战3 - 验证 int *[3] 和 int (*)[3] 的 sizeof 差异
 */

#include <stdio.h>

int main(void)
{
    /*====================================================================
     * 练习1：用三种不同方式输出数组
     * 给定 arr[6] = {1, 3, 5, 7, 9, 11}，分别用三种方式遍历输出。
     *====================================================================*/
    int arr[6] = {1, 3, 5, 7, 9, 11};

    printf("练习1：三种遍历方式\n");

    /* 方式1：下标法——最直观，arr[i] 等价于 *(arr + i) */
    printf("  下标法：");
    for (int i = 0; i < 6; i++) printf("%d ", arr[i]);
    printf("\n");

    /* 方式2：指针偏移法——用数组名做基地址，加偏移量解引用 */
    printf("  指针偏移法：");
    for (int i = 0; i < 6; i++) printf("%d ", *(arr + i));
    printf("\n");

    /* 方式3：移动指针法——独立指针变量，逐步后移
     * 注意 *p++ 的运算顺序：后缀++优先级高于*，
     * 但后缀++是"先用后加"，所以先取 *p，再 p++。
     */
    printf("  移动指针法：");
    int *p = arr;
    for (int i = 0; i < 6; i++) printf("%d ", *p++);
    printf("\n\n");

    /*====================================================================
     * 练习2：验证 sizeof 的区别
     * sizeof(arr) → 整个数组的字节数（6 × sizeof(int) = 24）
     * sizeof(p)   → 指针变量本身的字节数（64位系统通常为8）
     *====================================================================*/
    printf("练习2：sizeof 区别\n");
    printf("  sizeof(arr) = %d（整个数组，6×4=24 字节）\n", (int)sizeof(arr));
    printf("  sizeof(p)   = %d（指针本身，64位系统通常为8字节）\n\n", (int)sizeof(p));

    /*====================================================================
     * 练习3：指针数组和数组指针的简单演示
     *
     * 指针数组 int *ptrArr[3]：
     *   - ptrArr 是一个数组，包含 3 个元素，每个元素是 int*
     *   - [] 优先级高于 *，所以 ptrArr 先和 [3] 结合成数组
     *
     * 数组指针 int (*arrPtr)[3]：
     *   - arrPtr 是一个指针，指向一个 int[3] 数组
     *   - 括号强制 * 先和 arrPtr 结合，说明它是指针
     *====================================================================*/
    int a = 10, b = 20, c = 30;

    /* 指针数组：数组里存指针 */
    int *ptrArr[3] = {&a, &b, &c};
    printf("练习3：指针数组 —— int *ptrArr[3]\n");
    printf("  ptrArr 是数组，包含 3 个 int* 指针\n");
    for (int i = 0; i < 3; i++)
    {
        printf("  ptrArr[%d] 指向的值：%d\n", i, *ptrArr[i]);
    }

    /* 数组指针：指向整个数组 */
    int matrix[3] = {100, 200, 300};
    int (*arrPtr)[3] = &matrix;   /* arrPtr 指向整个 matrix 数组 */
    printf("\n  数组指针 —— int (*arrPtr)[3]\n");
    printf("  arrPtr 是指针，指向一个 int[3] 数组\n");
    for (int i = 0; i < 3; i++)
    {
        /* 先解引用得到数组，再取下标 */
        printf("  (*arrPtr)[%d] = %d\n", i, (*arrPtr)[i]);
    }

    printf("\n========================================\n");
    printf("挑战题提示（请自行实现）：\n");
    printf("  1. 数组逆序——分别用下标法和指针法实现\n");
    printf("     void reverse_by_index(int arr[], int n);\n");
    printf("     void reverse_by_ptr(int *arr, int n);\n");
    printf("  2. 遍历二维数组 int mat[2][3]：\n");
    printf("     int *p = &mat[0][0]; for(i=0;i<6;i++) printf(\"%%d \", *p++);\n");
    printf("  3. sizeof(int *[3]) vs sizeof(int (*)[3])：\n");
    printf("     前者是数组(3个指针大小)，后者是指针(一个指针大小)\n");

    return 0;
}
