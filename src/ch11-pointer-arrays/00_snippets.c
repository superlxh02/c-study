/**
 * @file    00_snippets.c
 * @chapter 第十一章 - 指针和数组的关系
 * @section 一.1 ~ 二.1.3 - 指针与数组等价性 / 区别片段集
 * @brief   演示：malloc指针作数组用、数组名退化、sizeof区别、取地址区别、
 *          数组名不可修改、指针算术运算、指针数组与数组指针声明。
 *
 * 本文件将第八章各个小节中的小代码片段汇总到一起，方便集中编译运行和对比观察。
 * 每个片段用一节注释分隔。运行时需要逐个解除注释来观察效果。
 */

#include <stdio.h>
#include <stdlib.h>

int main(void)
{
    /*====================================================================
     * 片段1：malloc 返回的指针可以用下标访问（第十一章 一.1）
     * 说明：p[i] 被编译器翻译成 *(p + i)，所以指针也能像数组一样使用下标。
     *====================================================================*/
    printf("===== 片段1：malloc指针当数组用 =====\n");
    {
        int *p = malloc(5 * sizeof(int));  // 在堆上申请 5 个 int 的空间
        p[0] = 10;
        p[1] = 20;
        p[2] = 30;
        p[3] = 40;
        p[4] = 50;
        printf("p[2] = %d  (完全像数组！)\n", p[2]);
        free(p);  // 用完后释放堆内存
    }
    printf("\n");

    /*====================================================================
     * 片段2：数组名代表首地址（第十一章 一.1）
     * 说明：arr 的值和 &arr[0] 完全相同，都指向数组的首元素。
     *====================================================================*/
    printf("===== 片段2：数组名代表首地址 =====\n");
    {
        int arr[5] = {1, 2, 3, 4, 5};
        printf("arr     = %p\n", (void *)arr);
        printf("&arr[0] = %p\n", (void *)&arr[0]);
        printf("两者数值完全一样\n");
    }
    printf("\n");

    /*====================================================================
     * 片段3：数组名不能重新赋值（第十一章 一.2）
     * 说明：数组名是常量，不能放在等号左边被赋值；而指针变量可以。
     *====================================================================*/
    printf("===== 片段3：数组名不可赋值 =====\n");
    {
        int arr[5];
        int *p = arr;     /* 正确：把数组首地址赋给指针 */
        printf("p = %p (指向 arr 首元素)\n", (void *)p);
        /* arr = p; */    /* 错误！数组名不能放在等号左边被赋值 */
        printf("注释掉的那行 (arr = p;) 会导致编译错误\n");
    }
    printf("\n");

    /*====================================================================
     * 片段4：arr[i] 等价于 *(arr + i)（第十一章 一.3）
     * 说明：编译器将 arr[i] 翻译为 *(arr + i)，指针算术自动按类型缩放。
     *====================================================================*/
    printf("===== 片段4：下标与指针偏移等价 =====\n");
    {
        int arr[5] = {10, 20, 30, 40, 50};
        printf("arr[2]   = %d\n", arr[2]);      /* 下标法 */
        printf("*(arr+2) = %d\n", *(arr + 2));  /* 指针偏移法——完全等价 */
        printf("arr[2] 本质上就是 *(arr + 2)\n");
    }
    printf("\n");

    /*====================================================================
     * 片段5：sizeof 的区别（第十一章 二.1.1）
     * 说明：sizeof(数组名) 得整个数组大小；sizeof(指针) 得指针变量自身大小。
     *====================================================================*/
    printf("===== 片段5：sizeof 的区别 =====\n");
    {
        int arr[5] = {1, 2, 3, 4, 5};
        int *p = arr;
        printf("sizeof(arr) = %d (整个数组的字节数，5×4=20)\n", (int)sizeof(arr));
        printf("sizeof(p)   = %d (指针变量本身的大小，64位系统通常为8)\n", (int)sizeof(p));
        printf("p 当前指向 arr[0]，其值为 %d\n", *p);
    }
    printf("\n");

    /*====================================================================
     * 片段6：取地址行为的区别（第十一章 二.1.2）
     * 说明：arr 退化为首元素地址(int*)，&arr 是整个数组地址(int(*)[5])；
     *       对于指针变量，p 的值和 &p 完全不同。
     *====================================================================*/
    printf("===== 片段6：取地址行为的区别 =====\n");
    {
        int arr[5];
        /* arr 和 &arr 的数值相同，但类型不同 */
        printf("arr    = %p (类型：int *       ——首元素地址)\n", (void *)arr);
        printf("&arr   = %p (类型：int (*)[5]  ——整个数组地址)\n", (void *)&arr);
        printf("两者数值相同，但类型不同！\n\n");

        int *p = arr;
        printf("p  = %p (p 的值：指向 arr 首地址)\n", (void *)p);
        printf("&p = %p (指针变量 p 自己的地址——和 p 的值完全不同！)\n", (void *)&p);
    }
    printf("\n");

    /*====================================================================
     * 片段7：数组名不可修改（第十一章 二.1.3）
     * 说明：p++ 合法，arr++ 非法——数组名是地址常量。
     *====================================================================*/
    printf("===== 片段7：数组名不可修改 =====\n");
    {
        int arr[5];
        int *p = arr;
        p++;       /* 正确：p 是变量，可以改变指向 */
        printf("p++ 之后 p = %p (指向 arr[1])\n", (void *)p);
        /* arr++; */ /* 错误！数组名是常量，不能改变 */
        printf("注释掉的 arr++ 会导致编译错误\n");
    }
    printf("\n");

    /*====================================================================
     * 片段8：指针数组与数组指针声明（第十一章 三.1）
     * 说明：int *arr[5] 是数组(元素为指针)；int (*p)[5] 是指针(指向数组)。
     *       区分关键在于 [] 优先级高于 *，加括号可改变结合顺序。
     *====================================================================*/
    printf("===== 片段8：指针数组 vs 数组指针 =====\n");
    {
        /* 指针数组：arr 是数组，里面存 3 个 int* 指针 */
        int a = 10, b = 20, c = 30;
        int *ptrArr[3] = {&a, &b, &c};
        printf("int *ptrArr[3]  —— 指针数组(数组，元素是指针)\n");
        printf("sizeof(ptrArr) = %d (3个指针的大小)\n", (int)sizeof(ptrArr));
        printf("*ptrArr[0] = %d (第一个指针指向 a)\n", *ptrArr[0]);

        /* 数组指针：p 是指针，指向一个包含 3 个 int 的数组 */
        int matrix[3] = {100, 200, 300};
        int (*arrPtr)[3] = &matrix;
        printf("\nint (*arrPtr)[3] —— 数组指针(指针，指向整个数组)\n");
        printf("sizeof(arrPtr) = %d (指针本身的大小)\n", (int)sizeof(arrPtr));
        printf("(*arrPtr)[0] = %d (经数组指针访问首元素)\n", (*arrPtr)[0]);
    }

    return 0;
}
