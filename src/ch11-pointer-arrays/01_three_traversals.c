/**
 * @file    01_three_traversals.c
 * @chapter 第十一章 - 指针和数组的关系
 * @section 一.4 - 用指针遍历数组
 * @brief   演示遍历数组的三种写法：下标法、指针偏移法、移动指针法。
 *
 * 三种方法输出完全相同，但底层原理都是指针算术运算：
 *   arr[i] 等价于 *(arr + i) 等价于 *p (p 逐个递增)
 */

#include <stdio.h>

int main(void)
{
    int arr[5] = {10, 20, 30, 40, 50};

    /*--------------------------------------------------------------------
     * 方法1：下标法（最常用、最直观）
     * 直接通过 arr[i] 访问第 i 个元素。
     * 编译器内部将 arr[i] 翻译为 *(arr + i)。
     *--------------------------------------------------------------------*/
    printf("下标法：");
    for (int i = 0; i < 5; i++)
    {
        printf("%d ", arr[i]);
    }
    printf("\n");

    /*--------------------------------------------------------------------
     * 方法2：指针偏移法（用数组名 + 偏移量）
     * *(arr + i) 表示从首地址跳过 i 个元素后的值。
     * 指针加法自动按类型缩放：arr + i 实际地址 = arr + i * sizeof(int)。
     *--------------------------------------------------------------------*/
    printf("指针偏移法：");
    for (int i = 0; i < 5; i++)
    {
        printf("%d ", *(arr + i));
    }
    printf("\n");

    /*--------------------------------------------------------------------
     * 方法3：移动指针法（用独立指针变量逐步后移）
     * p 初始指向 arr[0]，每次循环 *p 取当前值，然后 p++ 移到下一个元素。
     * p++ 使指针增加 sizeof(int) 字节，恰好指向下一个 int。
     * 注意：arr 本身不能++（数组名是常量），必须使用独立的指针变量。
     *--------------------------------------------------------------------*/
    printf("移动指针法：");
    int *p = arr;                /* p 指向数组首元素 */
    for (int i = 0; i < 5; i++)
    {
        printf("%d ", *p);
        p++;                      /* p 移动到下一个元素 */
    }
    printf("\n");

    return 0;
}
