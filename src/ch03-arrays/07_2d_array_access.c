#include <stdio.h>

int main(void)
{
    // 声明一个 5 行 3 列的二维数组，并按行初始化
    // 5 行代表 5 个学生，3 列代表 3 门课（语文、数学、英语）
    // 行下标范围：0~4，列下标范围：0~2
    int scores[5][3] = {
        {85, 92, 78},  // 第 0 行：学生 1 的三门课成绩
        {90, 88, 95},  // 第 1 行：学生 2 的三门课成绩
        {76, 82, 89},  // 第 2 行：学生 3 的三门课成绩
        {93, 87, 91},  // 第 3 行：学生 4 的三门课成绩
        {80, 85, 84}   // 第 4 行：学生 5 的三门课成绩
    };

    // ----- 通过 [行下标][列下标] 访问特定元素 -----
    // 格式：数组名[行下标][列下标]
    // 行下标和列下标都从 0 开始

    // 访问第 1 个学生（行下标 0）的第 2 门课（列下标 1）
    // scores[0] 是第 1 行数组，scores[0][1] 是该行的第 2 个元素
    printf("第1个学生第2门课：%d\n", scores[0][1]);  // 预期输出：92

    // 访问第 3 个学生（行下标 2）的第 1 门课（列下标 0）
    printf("第3个学生第1门课：%d\n", scores[2][0]);  // 预期输出：76

    return 0;
}

/*
 * ============================================================
 * 预期输出：
 * ------------------------------------------------------------
 * 第1个学生第2门课：92
 * 第3个学生第1门课：76
 * ============================================================
 *
 * 二维数组内存布局（5 行 × 3 列）：
 *
 *        列0    列1    列2
 * 行0  [ 85 ] [ 92 ] [ 78 ]   ← 学生 1
 * 行1  [ 90 ] [ 88 ] [ 95 ]   ← 学生 2
 * 行2  [ 76 ] [ 82 ] [ 89 ]   ← 学生 3
 * 行3  [ 93 ] [ 87 ] [ 91 ]   ← 学生 4
 * 行4  [ 80 ] [ 85 ] [ 84 ]   ← 学生 5
 *
 * scores[0][1] = 第 0 行第 1 列 = 92（学生 1 的数学成绩）
 * scores[2][0] = 第 2 行第 0 列 = 76（学生 3 的语文成绩）
 *
 * 要点总结：
 * 1. 二维数组用 scores[行][列] 的形式访问单个元素
 * 2. 行下标和列下标都从 0 开始
 * 3. 第一个方括号选行，第二个方括号选列
 * 4. 内存中按行优先顺序连续存储（一行接一行）
 */
