#include <stdio.h>

int main(void)
{
    // ============================================================
    // 练习 1：逆序输出数组
    // 目标：将数组元素从最后一个到第一个依次输出
    // ============================================================
    int arr[6] = {1, 2, 3, 4, 5, 6};

    // 先输出原数组，方便对比
    printf("原数组：");
    for (int i = 0; i < 6; i++)
    {
        printf("%d ", arr[i]);
    }
    printf("\n");

    // 逆序输出：循环变量 i 从 5（最后一个元素的下标）开始
    // 每次递减 1（i--），直到 i == -1 时循环条件 i >= 0 为假
    printf("逆序输出：");
    for (int i = 5; i >= 0; i--)  // 从最后一个元素开始，往前遍历
    {
        printf("%d ", arr[i]);
    }
    printf("\n\n");

    // ============================================================
    // 练习 2：统计数组中奇数和偶数的个数
    // 目标：遍历数组，根据每个元素对 2 取余的结果分类统计
    // ============================================================
    int numbers[10] = {12, 7, 33, 8, 15, 24, 9, 40, 3, 18};
    int oddCount = 0;   // 奇数计数器，初始为 0
    int evenCount = 0;  // 偶数计数器，初始为 0

    for (int i = 0; i < 10; i++)
    {
        // 判断奇偶：偶数 % 2 == 0，奇数 % 2 != 0
        if (numbers[i] % 2 == 0)  // 如果除以 2 的余数为 0，说明是偶数
        {
            evenCount++;  // 偶数计数 +1
        }
        else
        {
            oddCount++;   // 奇数计数 +1
        }
    }
    printf("数组中有 %d 个偶数，%d 个奇数\n\n", evenCount, oddCount);

    // ============================================================
    // 练习 3：二维数组——矩阵转置
    // 目标：输出一个 3×3 矩阵的转置（行和列互换）
    // 转置操作：原矩阵的 matrix[i][j] 在转置后变为 matrix[j][i]
    // ============================================================
    int matrix[3][3] = {
        {1, 2, 3},  // 第 0 行
        {4, 5, 6},  // 第 1 行
        {7, 8, 9}   // 第 2 行
    };

    // 输出原矩阵（按正常行列顺序）
    printf("原矩阵：\n");
    for (int i = 0; i < 3; i++)
    {
        for (int j = 0; j < 3; j++)
        {
            printf("%d ", matrix[i][j]);  // 行优先输出
        }
        printf("\n");  // 每行结束后换行
    }

    // 输出转置矩阵：关键变化是 matrix[j][i] 而不是 matrix[i][j]
    // 将行和列的循环变量"交换位置"即可实现转置效果
    printf("转置矩阵：\n");
    for (int i = 0; i < 3; i++)
    {
        for (int j = 0; j < 3; j++)
        {
            printf("%d ", matrix[j][i]);  // 行列互换！输出转置后的元素
        }
        printf("\n");
    }

    return 0;
}

/*
 * ============================================================
 * 预期输出：
 * ------------------------------------------------------------
 * 原数组：1 2 3 4 5 6
 * 逆序输出：6 5 4 3 2 1
 *
 * 数组中有 5 个偶数，5 个奇数
 *
 * 原矩阵：
 * 1 2 3
 * 4 5 6
 * 7 8 9
 * 转置矩阵：
 * 1 4 7
 * 2 5 8
 * 3 6 9
 * ============================================================
 *
 * 各练习要点：
 *
 * 练习 1（逆序输出）：
 * - 正序遍历：for (i = 0; i < N; i++)
 * - 逆序遍历：for (i = N-1; i >= 0; i--)
 * - 注意循环变量的起始值、终止条件和步进方向
 *
 * 练习 2（统计奇偶）：
 * - 判断偶数的条件：x % 2 == 0
 * - 判断奇数的条件：x % 2 != 0
 * - 使用两个独立的计数变量分别统计
 *
 * 练习 3（矩阵转置）：
 * - 原矩阵访问：matrix[i][j]（正常行列顺序）
 * - 转置访问：matrix[j][i]（交换行和列的下标位置）
 * - 转置不改变原数组内容，只是改变了读取/输出的顺序
 */
