#include <stdio.h>

// ============================================================
// 片段1：一维数组的四种初始化方式
// ============================================================

// 方式一：完全初始化 —— 在声明时列出所有元素的值
int scores1[5] = {85, 92, 78, 90, 88};
// 内存视图：{85, 92, 78, 90, 88}

// 方式二：部分初始化 —— 只给出部分值，剩余元素自动归零
int scores2[5] = {85, 92};
// 内存视图：{85, 92, 0, 0, 0}
// 关键区别：未指定的元素会被编译器自动初始化为 0

// 方式三：省略大小的初始化 —— 编译器根据初始化列表自动推断大小
int scores3[] = {85, 92, 78, 90, 88};  // 编译器推断大小为 5
// 注意：只有声明的同时初始化才可以省略大小！

// 方式四：逐个赋值 —— 先声明，再单独给每个元素赋值
/*
int scores4[5];
scores4[0] = 85;
scores4[1] = 92;
scores4[2] = 78;
scores4[3] = 90;
scores4[4] = 88;
*/
// 警告：逐个赋值时，未赋值的自动数组元素具有不确定值，
//       与部分初始化不同（部分初始化时未指定元素自动归零）。

// ============================================================
// 片段2：用宏定义数组大小 —— 避免"魔法数字"
// ============================================================
#define SIZE 5  // 用宏定义数组大小，一处修改、处处生效

/*
int scores[SIZE];
for (int i = 0; i < SIZE; i++)  // 使用常量，不容易写错
{
    // 遍历处理每个元素...
}
*/
// 好处：如果要改变数组大小，只需修改 #define 一处即可，
//       避免在代码中到处修改数字，减少遗漏和越界风险。

// ============================================================
// 片段3：二维数组初始化的两种写法
// ============================================================

// 方式一：按行初始化（推荐 —— 结构清晰，可读性强）
/*
int scores[5][3] = {
    {85, 92, 78},  // 第 0 行：第一个学生的 3 门课
    {90, 88, 95},  // 第 1 行
    {76, 82, 89},  // 第 2 行
    {93, 87, 91},  // 第 3 行
    {80, 85, 84}   // 第 4 行
};
*/

// 方式二：线性初始化（按内存顺序平铺）
/*
int scores[5][3] = {85, 92, 78, 90, 88, 95, 76, 82, 89, 93, 87, 91, 80, 85, 84};
*/
// 两种方式效果完全一样，内存布局相同。推荐使用方式一（按行初始化）。

// ============================================================
// 片段4：数组遍历的标准模式（for 循环）
// ============================================================

// 一维数组遍历标准模式：
// for (int i = 0; i < 数组大小; i++)
// {
//     // 通过 数组名[i] 访问每个元素
// }

// 二维数组遍历标准模式（嵌套循环）：
// for (int i = 0; i < 行数; i++)       // 外层循环控制行
// {
//     for (int j = 0; j < 列数; j++)    // 内层循环控制列
//     {
//         // 通过 数组名[i][j] 访问每个元素
//     }
// }

// ============================================================
// 片段5：数组语法规则速查
// ============================================================

// 一维数组：
//   声明  ：数据类型 数组名[大小];
//   初始化：数据类型 数组名[大小] = {值1, 值2, ...};
//   访问  ：数组名[下标]           // 下标从 0 开始，范围 0 ~ 大小-1

// 二维数组：
//   声明  ：数据类型 数组名[行数][列数];
//   初始化：数据类型 数组名[行数][列数] = {{...}, {...}, ...};
//   访问  ：数组名[行下标][列下标]

// 核心规则：
//   1. 数组是相同类型数据的集合，内存中连续存放
//   2. 下标从 0 开始，大小为 N 则有效下标为 0 ~ N-1
//   3. C 语言不检查数组越界，越界后果不可预测
//   4. 遍历一维数组用单层 for，遍历二维数组用嵌套 for
//   5. 部分初始化时未指定元素自动归零
