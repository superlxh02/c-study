#include <stdio.h>

int main(void)
{
    // 声明并初始化一个包含 5 个成绩的数组
    int scores[5] = {85, 92, 78, 90, 88};

    // ----- 用 for 循环遍历数组 -----
    // 标准遍历模式：for (int i = 0; i < 数组大小; i++)
    // i 从 0 开始，每次递增 1，直到 i == 5 时条件为假、退出循环
    // 循环共执行 5 次，i 依次为：0, 1, 2, 3, 4
    for (int i = 0; i < 5; i++)
    {
        // i + 1 将计算机编号（从0开始）转换为人类习惯的编号（从1开始）
        // scores[i] 读取当前下标对应的成绩值
        printf("第 %d 个同学的成绩：%d\n", i + 1, scores[i]);
    }

    return 0;
}

/*
 * ============================================================
 * 预期输出：
 * ------------------------------------------------------------
 * 第 1 个同学的成绩：85
 * 第 2 个同学的成绩：92
 * 第 3 个同学的成绩：78
 * 第 4 个同学的成绩：90
 * 第 5 个同学的成绩：88
 * ============================================================
 *
 * 执行过程分析（逐轮次）：
 * | 轮次 | i | i<5? | 访问元素      | 输出值 |
 * |------|---|------|--------------|--------|
 * | 1    | 0 | 真   | scores[0]    | 85     |
 * | 2    | 1 | 真   | scores[1]    | 92     |
 * | 3    | 2 | 真   | scores[2]    | 78     |
 * | 4    | 3 | 真   | scores[3]    | 90     |
 * | 5    | 4 | 真   | scores[4]    | 88     |
 * | 6    | 5 | 假   | (退出循环)    | —      |
 *
 * 要点总结：
 * 1. for (int i = 0; i < N; i++) 是遍历数组的标准写法
 * 2. 循环变量 i 从 0 开始，因为数组下标从 0 开始
 * 3. 条件是 i < 5（不是 i <= 5），因为有效下标最大是 4
 * 4. i++ 每次递增 1，按顺序访问每一个元素
 */
