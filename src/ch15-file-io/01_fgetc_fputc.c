/**
 * @file    01_fgetc_fputc.c
 * @chapter 第十五章 - 文件IO
 * @section 二.1.3 - 字符读写：fgetc / fputc 示例
 * @brief   演示用 fgetc 和 fputc 逐个字符复制文件。
 *
 * 核心知识点：
 *   1. fgetc(FILE*) —— 从文件读取一个字符，返回 int（便于判断 EOF）
 *   2. fputc(int c, FILE*) —— 向文件写入一个字符
 *   3. EOF (End Of File) 通常定义为 -1，用于判断文件结束
 *   4. 必须检查 fopen 返回值，失败时返回 NULL
 *   5. 打开的所有文件都要用 fclose 关闭
 */

#include <stdio.h>

int main(void)
{
    /*--------------------------------------------------------------------
     * 打开源文件 "source.txt"（只读模式）
     * 如果文件不存在，fopen 返回 NULL
     *--------------------------------------------------------------------*/
    FILE *src = fopen("source.txt", "r");
    if (src == NULL)
    {
        printf("无法打开源文件 source.txt\n");
        printf("请先创建 source.txt 文件再运行本程序。\n");
        return 1;
    }

    /*--------------------------------------------------------------------
     * 打开目标文件 "copy.txt"（只写模式）
     * 如果文件存在则清空，不存在则创建
     *--------------------------------------------------------------------*/
    FILE *dest = fopen("copy.txt", "w");
    if (dest == NULL)
    {
        printf("无法打开目标文件 copy.txt\n");
        fclose(src);  /* 别忘了关闭已打开的源文件 */
        return 1;
    }

    /*--------------------------------------------------------------------
     * 核心逻辑：逐个字符读取并写入
     *
     * ch 声明为 int 而不是 char，因为：
     *   - fgetc 返回 int，需要能表示 0~255 的 unsigned char 值和 EOF(-1)
     *   - 如果用 char，EOF 可能和某个合法字符 (如 0xFF) 混淆
     *
     * while ((ch = fgetc(src)) != EOF) 是经典的 C 文件读取循环
     *   - 先调用 fgetc 读取一个字符
     *   - 将返回值赋给 ch
     *   - 判断 ch 是否等于 EOF
     *--------------------------------------------------------------------*/
    int ch;
    while ((ch = fgetc(src)) != EOF)
    {
        fputc(ch, dest);  /* 将读到的字符写入目标文件 */
    }

    /*--------------------------------------------------------------------
     * 关闭文件
     * 关闭后缓冲区中的数据会被刷新到磁盘，相关资源被释放
     *--------------------------------------------------------------------*/
    fclose(src);
    fclose(dest);

    printf("文件复制完成！source.txt → copy.txt\n");

    return 0;
}
