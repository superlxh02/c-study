/**
 * @file    06_wc_demo.c
 * @chapter 第十五章 - 文件IO
 * @section 四.1 - 动手练习：统计文件字数/字符数/行数
 * @brief   模拟 Unix wc 命令的基本功能：统计文件中的字符数、单词数和行数。
 *
 * 核心逻辑：
 *   字符数 - 每读到一个字符 chars++
 *   行数   - 每读到 '\n' 则 lines++
 *   单词数 - 用 inWord 状态机：空白字符标记出单词，非空白标记入单词
 *           从空白进入非空白时表示一个新单词开始，words++
 *
 * 附加挑战（在注释中给出了实现提示）：
 *   挑战1 - 用户输入多行文字写入文件，直到输入空行
 *   挑战2 - 学生记录管理（添加 + 保存 + 加载）
 *   挑战3 - 文件加密（XOR 异或）
 *   挑战4 - 倒序读取文件内容
 */

#include <stdio.h>
#include <stdlib.h>

int main(void)
{
    /*--------------------------------------------------------------------
     * 打开文件（只读模式）
     *--------------------------------------------------------------------*/
    FILE *fp = fopen("text.txt", "r");
    if (fp == NULL)
    {
        printf("文件 text.txt 不存在\n");
        printf("请先创建 text.txt 文件再运行本程序。\n");
        return 1;
    }

    /*--------------------------------------------------------------------
     * 统计变量
     *   chars  — 字符总数（包括空格、换行等所有字符）
     *   words  — 单词数（由空白字符分隔的连续非空白字符序列）
     *   lines  — 行数（由 '\n' 决定）
     *   inWord — 状态标志：0=当前在单词外部(空白区)，1=当前在单词内部
     *   lastChar — 记录最后一个字符，用于判断文件末尾是否缺换行
     *--------------------------------------------------------------------*/
    int chars = 0, words = 0, lines = 0;
    int inWord = 0;          /* 初始状态：不在单词内 */
    int lastChar = '\n';     /* 初始设为 '\n' */
    int ch;

    /*--------------------------------------------------------------------
     * 逐字符读取并统计
     *
     * 单词检测状态机：
     *   如果读到空白（空格、换行、制表符） → inWord = 0（离开单词）
     *   如果读到非空白且 inWord == 0     → inWord = 1，words++（新单词开始）
     *   如果读到非空白且 inWord == 1     → 继续当前单词，不计数
     *--------------------------------------------------------------------*/
    while ((ch = fgetc(fp)) != EOF)
    {
        chars++;                    /* 统计字符数 */
        if (ch == '\n') lines++;    /* 统计行数（每个换行符算一行） */
        lastChar = ch;              /* 记录最后一个字符 */

        /* 单词状态机 */
        if (ch == ' ' || ch == '\n' || ch == '\t')
            inWord = 0;             /* 遇到空白：离开单词 */
        else if (inWord == 0)
        {
            inWord = 1;             /* 从空白进入非空白：新单词开始 */
            words++;
        }
    }
    fclose(fp);

    /*--------------------------------------------------------------------
     * 处理文件末尾可能缺少换行的情况
     * 如果文件非空且最后一个字符不是换行符，则最后一行未被统计
     *--------------------------------------------------------------------*/
    if (chars > 0 && lastChar != '\n')
        lines++;

    /*--------------------------------------------------------------------
     * 输出统计结果
     *--------------------------------------------------------------------*/
    printf("===== 文件 text.txt 统计结果 =====\n\n");
    printf("  字符数：%d\n", chars);
    printf("  单词数：%d\n", words);
    printf("  行数：%d\n", lines);

    printf("\n========================================\n");
    printf("挑战题提示（请自行实现）：\n");
    printf("  1. 多行输入写入文件：\n");
    printf("     fgets(buf, sizeof(buf), stdin);\n");
    printf("     while(buf[0] != '\\n') { fputs(buf, fp); fgets(...); }\n");
    printf("  2. 学生记录管理：\n");
    printf("     fwrite/fread 读写 struct Student 数组，\n");
    printf("     启动时加载、退出时保存\n");
    printf("  3. 文件加密 (XOR)：\n");
    printf("     while((ch=fgetc(src))!=EOF) fputc(ch ^ key, dest);\n");
    printf("     再运行一次自动解密（XOR 两次还原）\n");
    printf("  4. 倒序读取文件：\n");
    printf("     fseek(fp, -1, SEEK_CUR) 逐步回退 + fgetc 读取\n");

    return 0;
}
