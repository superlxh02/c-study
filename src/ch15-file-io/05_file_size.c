/**
 * @file    05_file_size.c
 * @chapter 第十五章 - 文件IO
 * @section 三.1.4 - 文件定位：fseek / ftell 获取文件大小
 * @brief   演示使用 fseek + ftell 获取文件字节数。
 *
 * 核心知识点：
 *   1. fseek(fp, offset, whence) —— 移动文件位置指示器
 *      whence: SEEK_SET(开头), SEEK_CUR(当前), SEEK_END(末尾)
 *   2. ftell(fp) —— 返回当前字节偏移量（距文件开头）
 *   3. rewind(fp) —— 等价于 fseek(fp, 0, SEEK_SET)，回到开头
 *   4. 计算文件大小时推荐用 "rb" 二进制模式打开
 *      （文本模式在某些系统上可能因换行转换导致 ftell 结果不准）
 */

#include <stdio.h>

int main(void)
{
    /*--------------------------------------------------------------------
     * 用二进制模式 "rb" 打开文件
     * 这确保了 ftell 返回的值等同于文件的实际字节数
     *--------------------------------------------------------------------*/
    FILE *fp = fopen("data.txt", "rb");
    if (fp == NULL)
    {
        printf("无法打开文件 data.txt\n");
        printf("请先创建 data.txt 文件再运行本程序。\n");
        return 1;
    }

    /*--------------------------------------------------------------------
     * 步骤1：移动到文件末尾
     * fseek(fp, 0, SEEK_END)
     *   0       —— 偏移量为 0（从 SEEK_END 开始偏移 0 字节 = 恰好末尾）
     *   SEEK_END —— 以文件末尾为基准点
     *--------------------------------------------------------------------*/
    fseek(fp, 0, SEEK_END);

    /*--------------------------------------------------------------------
     * 步骤2：获取当前位置（即文件总字节数）
     * ftell 返回当前位置指示器距文件开头的字节偏移量
     * 因为刚才移动到了末尾，所以返回值就是文件大小
     *
     * 返回值类型为 long，在大多数平台上可表示的文件大小足够大
     *--------------------------------------------------------------------*/
    long size = ftell(fp);      /* 二进制模式下通常可用作字节数 */
    printf("文件 data.txt 的大小：%ld 字节\n", size);

    /*--------------------------------------------------------------------
     * 步骤3：回到文件开头
     * rewind(fp) 等价于 fseek(fp, 0, SEEK_SET)
     * 如果之后还要进行其他读写操作，需要先回到开头
     *--------------------------------------------------------------------*/
    rewind(fp);  /* 回到开头继续操作 */

    /* 验证确实回到了开头 */
    long pos = ftell(fp);
    printf("回到开头后位置指示器：%ld（应为0）\n", pos);

    fclose(fp);

    printf("\n提示：计算文件字节数时建议用 \"rb\" 二进制模式打开。\n");
    printf("文本模式下，某些系统会进行换行转换(\\n→\\r\\n)，\n");
    printf("导致 ftell 结果不等于真实字节数。\n");

    return 0;
}
