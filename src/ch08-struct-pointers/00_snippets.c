/**
 * @file    00_snippets.c
 * @chapter 第八章 - 结构体指针
 * @section 一.1 ~ 一.2 - 结构体指针基础片段集
 * @brief   演示：通过指针访问结构体成员、(*p).name 与 p->name 的等价性、
 *          箭头运算符 -> 的基本用法。
 *
 * 本文件汇总了第八章中各个小节的小代码片段，方便集中观察结构体指针的核心语法。
 */

#include <stdio.h>

struct Student
{
    char name[20];
    int age;
    float score;
};

int main(void)
{
    /*====================================================================
     * 片段1：用 * 解引用后再用 . 访问成员（第八章 一.1）
     * 这是最直接但略显笨重的写法：先解引用指针得到结构体变量，再用 . 取成员。
     * 由于 . 优先级高于 *，必须加括号写成 (*p).name。
     *====================================================================*/
    printf("===== 片段1：(*p).成员 方式 =====\n");
    {
        struct Student stu1 = {"小明", 18, 92.5};
        struct Student *p = &stu1;       /* p 是指向 stu1 的指针 */

        printf("(*p).name  = %s\n", (*p).name);   /* 先解引用得到结构体，再用 . 取成员 */
        printf("(*p).age   = %d\n", (*p).age);
        printf("(*p).score = %.1f\n", (*p).score);
        printf("这种写法可行，但每次要多打括号和星号，比较笨重\n");
    }
    printf("\n");

    /*====================================================================
     * 片段2：箭头运算符 -> 等价于 (*ptr).成员（第八章 一.2）
     * p->name 等价于 (*p).name，更简洁直观。
     * -> 左侧必须是结构体指针，右侧必须是成员名。
     *====================================================================*/
    printf("===== 片段2：p->成员 方式（箭头运算符） =====\n");
    {
        struct Student stu1 = {"小明", 18, 92.5};
        struct Student *p = &stu1;

        /* 以下两行完全等价 */
        printf("(*p).name = %s\n", (*p).name);
        printf("p->name   = %s    (箭头运算符更简洁)\n", p->name);

        /* 通过指针修改成员——同样使用 -> */
        p->age = 19;
        p->score = 95.0;
        printf("修改后：年龄 = %d, 成绩 = %.1f\n", p->age, p->score);
    }
    printf("\n");

    /*====================================================================
     * 片段3：结构体指针的声明与赋值总结
     * 声明：struct Student *p;
     * 赋值：p = &stu1;  或  p = students;（指向数组首元素）
     *====================================================================*/
    printf("===== 片段3：结构体指针声明与赋值 =====\n");
    {
        struct Student stu1 = {"小红", 20, 88.5};
        struct Student *p;            /* 声明一个结构体指针 */

        p = &stu1;                    /* 让 p 指向 stu1 */
        printf("p->name  = %s\n", p->name);
        printf("p->age   = %d\n", p->age);
        printf("p->score = %.1f\n", p->score);
    }

    return 0;
}
