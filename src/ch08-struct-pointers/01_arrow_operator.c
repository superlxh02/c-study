/**
 * @file    01_arrow_operator.c
 * @chapter 第八章 - 结构体指针
 * @section 一.2.3 - 箭头运算符 -> 语法示例
 * @brief   演示箭头运算符 -> 的完整用法：通过结构体指针访问和修改成员。
 *
 * 核心知识点：
 *   1. p->成员 等价于 (*p).成员
 *   2. -> 左侧必须是指向结构体的指针，右侧是成员名
 *   3. -> 由减号 - 和大于号 > 组成，中间不能有空格
 */

#include <stdio.h>

/* 定义学生结构体 */
struct Student
{
    char name[20];   /* 姓名 */
    int age;         /* 年龄 */
    float score;     /* 成绩 */
};

int main(void)
{
    /* 创建结构体变量并初始化 */
    struct Student stu1 = {"小明", 18, 92.5};

    /* 声明结构体指针，指向 stu1 */
    struct Student *p = &stu1;   /* p 指向 stu1 */

    /*--------------------------------------------------------------------
     * 对比两种访问方式：
     *   (*p).name —— 先解引用得到结构体，再用 . 取成员（括号不可省略）
     *   p->name   —— 箭头运算符，更简洁直观
     *--------------------------------------------------------------------*/
    printf("===== 箭头运算符 -> 演示 =====\n\n");

    /* 以下两行完全等价，输出相同的值 */
    printf("使用 (*p).成员：\n");
    printf("  姓名：%s\n", (*p).name);
    printf("  年龄：%d\n", (*p).age);
    printf("  成绩：%.1f\n\n", (*p).score);

    printf("使用 p->成员：\n");
    printf("  姓名：%s\n", p->name);    /* 用 -> 更简洁 */
    printf("  年龄：%d\n", p->age);
    printf("  成绩：%.1f\n\n", p->score);

    /*--------------------------------------------------------------------
     * 通过指针修改结构体成员
     * 使用 -> 运算符可以直接修改指针所指向的结构体的成员值
     *--------------------------------------------------------------------*/
    printf("===== 通过指针修改成员 =====\n\n");

    p->age = 19;       /* 修改年龄 */
    p->score = 95.0;   /* 修改成绩 */

    printf("修改后的信息：\n");
    printf("  年龄：%d\n", p->age);
    printf("  成绩：%.1f\n", p->score);

    /* 验证 stu1 本身确实被修改了（因为 p 指向的就是 stu1） */
    printf("\n  stu1.age   = %d  (stu1 本身也被修改了)\n", stu1.age);
    printf("  stu1.score = %.1f\n", stu1.score);

    return 0;
}
