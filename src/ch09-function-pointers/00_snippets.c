#include <stdio.h>

/* =================================================================
 * 辅助函数：为片段提供可指向的函数实体
 * ================================================================= */
static int add_func(int a, int b)    { return a + b; }
static int sub_func(int a, int b)    { return a - b; }

/* =================================================================
 * 【片段1】函数指针声明（对应 第九章 一-2.3）
 * 语法：返回值类型 (*指针名)(参数类型列表);
 * ================================================================= */
void demo_func_ptr_declaration(void)
{
    printf("====== 片段1：函数指针声明 ======\n");

    /* 声明一个函数指针 pFunc：
     *   - 指向"返回值为 int、接受两个 int 参数"的函数
     *   - (*pFunc) 的括号必须加，否则含义完全不同
     */
    int (*pFunc)(int, int);   /* pFunc 是函数指针 */

    /* 把 add_func 的地址赋给 pFunc */
    pFunc = add_func;         /* 函数名会自动转换为函数指针 */
    /* 或者写成 pFunc = &add_func; 效果完全相同 */

    /* 通过函数指针调用 */
    int result = pFunc(3, 5); /* 等价于 add_func(3, 5) */
    /* 也可以写成 (*pFunc)(3, 5); 效果相同 */

    printf("通过函数指针 pFunc(3, 5) 调用 add_func 的结果：%d\n", result);
    /* 预期输出：通过函数指针 pFunc(3, 5) 调用 add_func 的结果：8 */

    printf("\n");
}

/* =================================================================
 * 【片段2】不加括号会怎样？（对应 第九章 一-2.4）
 * int *pFunc(int, int);  → 这不是函数指针！
 *                         → 这是声明一个名为 pFunc 的普通函数，返回 int*
 * ================================================================= */
void demo_without_parentheses(void)
{
    printf("====== 片段2：不加括号的区别 ======\n");

    printf("声明对比：\n");
    printf("  int (*pFunc)(int, int);   // 函数指针：pFunc 是指针，指向函数\n");
    printf("  int  *func(int, int);     // 普通函数：func 是函数，返回 int*\n");
    printf("\n");

    printf("原因：C 语言中 () 的优先级高于 *。\n");
    printf("  不加括号 → func 先与 (int,int) 结合 → 'func 是函数'\n");
    printf("  加了括号 → (*pFunc) 先结合    → 'pFunc 是指针'\n");
    printf("\n");

    printf("规则：声明函数指针时，星号两边必须加括号！\n");

    printf("\n");
}

/* =================================================================
 * 【片段3】typedef 简化函数指针类型（对应 第九章 三-3）
 * 用 typedef 给函数指针类型一个别名，后续声明更简洁
 * ================================================================= */
void demo_typedef_func_ptr(void)
{
    printf("====== 片段3：typedef 简化函数指针 ======\n");

    /* 定义 Operation 为函数指针类型：
     *   Operation 是"指向 返回int、接收两个int 的函数"的类型别名
     */
    typedef int (*Operation)(int, int);

    /* 现在用 Operation 声明函数指针，和声明普通变量一样简洁 */
    Operation op;              /* op 是函数指针变量 */
    op = sub_func;

    int a = 12, b = 7;
    printf("使用 typedef 定义的函数指针调用 sub_func(%d, %d) = %d\n",
           a, b, op(a, b));
    /* 预期输出：使用 typedef 定义的函数指针调用 sub_func(12, 7) = 5 */

    /* 对比不用 typedef 的写法：
     *   int (*op)(int, int) = sub_func;   // 可读性较差
     * 使用 typedef 后：
     *   Operation op = sub_func;          // 清晰明了
     */

    printf("\n");
}

/* =================================================================
 * 【片段4】"函数指针" vs "指针函数"（对应 第九章 三-2）
 * 这是谭浩强式教材常见的文字游戏，本质上：
 *   - 函数指针：是指针，指向函数（本章核心）
 *   - 指针函数：是函数，返回指针（不是独立概念，普通函数而已）
 * ================================================================= */
void demo_func_ptr_vs_ptr_func(void)
{
    printf("====== 片段4：函数指针 vs 指针函数 ======\n");

    /* 函数指针：本质是指针，存储函数的地址 */
    int (*func_ptr)(int, int) = add_func;
    printf("  实际调用 func_ptr(2, 3) = %d\n", func_ptr(2, 3));
    printf("函数指针：int (*func_ptr)(int, int)  → func_ptr 是指针\n");
    printf("  它存的是函数地址，可以指向不同函数，实现回调。\n\n");

    /* 所谓"指针函数"：本质是函数，只是返回值是指针 */
    printf("指针函数：int *getArray() { ... }  → getArray 是函数\n");
    printf("  返回类型是 int*（指针），本质上就是普通函数。\n");
    printf("  这不是独立的知识点，只是返回值为指针的普通函数。\n\n");

    /* 对比总结 */
    printf("┌──────────┬────────┬───────────────────────────┐\n");
    printf("│ 术语     │ 本质   │ 示例                      │\n");
    printf("├──────────┼────────┼───────────────────────────┤\n");
    printf("│ 函数指针 │ 指针   │ int (*p)(int, int);       │\n");
    printf("│ 指针函数 │ 函数   │ int *f(int, int); 返回int*│\n");
    printf("└──────────┴────────┴───────────────────────────┘\n\n");

    printf("记忆诀窍：看最后一个字！\n");
    printf("  函数指针 → 指针（变量的门类）\n");
    printf("  指针函数 → 函数（变量的门类）\n");

    printf("\n");
}

/* =================================================================
 * main
 * ================================================================= */
int main(void)
{
    demo_func_ptr_declaration();
    demo_without_parentheses();
    demo_typedef_func_ptr();
    demo_func_ptr_vs_ptr_func();

    return 0;
}
