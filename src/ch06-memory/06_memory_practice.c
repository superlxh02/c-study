#include <stdio.h>

/*
 * 全局变量 —— 静态存储区
 * 从程序启动到结束，始终存在，地址不变
 */
int globalNum = 100;

/*
 * func 函数：观察函数参数、static 局部变量、普通局部变量的地址
 *
 * 参数 param 和局部变量 localNum —— 自动存储期，常见实现使用栈帧
 * static 变量 staticNum —— 静态存储期，值跨调用保留
 */
void func(int param)
{
    /*
     * static 局部变量 staticNum
     * 存储期：静态存储期
     * 静态初始化在程序启动前完成且只发生一次
     * 多次调用 func，staticNum 的地址始终不变
     */
    static int staticNum = 200;

    /*
     * 普通局部变量 localNum
     * 存储期：自动存储期
     * 每次调用 func 时创建新的对象实例
     * 函数返回时自动销毁
     *
     * 如果两次调用 func 的栈深度相同，localNum 可能在同一个地址上被创建
     * （但这依赖于编译器和优化级别，不是语言标准保证的行为）
     */
    int localNum = 300;

    printf("  函数 func 内部：\n");
    printf("  参数 param 的地址：        %p\n", (void *)&param);
    printf("  静态变量 staticNum 的地址：%p\n", (void *)&staticNum);
    printf("  局部变量 localNum 的地址： %p\n", (void *)&localNum);
}

int main(void)
{
    /*
     * main 中的局部变量 mainLocal
     * 存储期：自动存储期
     * 在进入 main 的块时开始生命周期，离开时结束
     */
    int mainLocal = 400;

    printf("=== 内存区域综合观察 ===\n\n");

    // 第一次调用 func
    printf("=== 第一次调用 func ===\n");
    func(10);

    // 第二次调用 func
    // 观察：staticNum 的地址是否和第一次一样？
    //       param 和 localNum 的地址呢？
    printf("\n=== 第二次调用 func ===\n");
    func(20);

    // 打印 main 中变量的地址
    printf("\n=== main 函数内部 ===\n");
    printf("全局变量 globalNum 的地址：      %p\n", (void *)&globalNum);
    printf("main 局部变量 mainLocal 的地址： %p\n", (void *)&mainLocal);

    printf("\n\n=== 思考题 ===\n");
    printf("1. 两次调用 func 时，staticNum 的地址相同吗？\n");
    printf("   答：应该相同 —— 因为它在静态存储区，位置不变\n\n");
    printf("2. 两次调用 func 时，param 和 localNum 的地址相同吗？\n");
    printf("   答：可能相同也可能不同，取决于编译器和调用时的栈状态。\n");
    printf("   如果相同，说明每次调用 func 时栈帧在同一位置；\n");
    printf("   如果不同（比如中间有递归或其他嵌套调用），说明栈在变化。\n\n");
    printf("3. 全局变量的地址和 mainLocal 的地址哪个更大？\n");
    printf("   答：C 标准没有规定，不能从某次运行推广到其他平台。\n");

    return 0;
}

