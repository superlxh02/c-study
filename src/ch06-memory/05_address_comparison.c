#include <stdio.h>

/*
 * 全局变量 —— 静态存储区
 * 定义在所有函数之外，程序启动时创建
 */
int globalVar = 1;

int main(void)
{
    /*
     * static 局部变量 —— 静态存储区
     * 虽然定义在 main 函数内部，但由于 static 修饰，
     * 它存放在静态存储区，而不是栈区
     */
    static int staticVar = 2;

    /*
     * 普通局部变量 —— 自动存储期
     * 常见实现可能放在栈帧中；取地址后编译器会提供可寻址存储
     */
    int localVar = 3;

    printf("=== 不同内存区域的地址比较 ===\n\n");

    /*
     * 这些地址可用于确认三个对象彼此独立，但不能据此推导可移植顺序。
     * 不同操作系统、ABI、链接方式、ASLR 和优化选项都可能改变布局。
     */
    printf("全局变量 globalVar 的地址：  %p\n", (void *)&globalVar);
    printf("静态变量 staticVar 的地址：  %p\n", (void *)&staticVar);
    printf("局部变量 localVar 的地址：  %p\n", (void *)&localVar);

    printf("\n观察分析：\n");
    printf("  三个对象都有可打印的地址，但地址大小和相互距离由实现决定。\n");
    printf("  不要用某次运行结果断言栈、静态数据或代码的固定顺序。\n");

    return 0;
}

