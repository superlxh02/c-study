#include <stdio.h>
#include <stdlib.h>   // malloc、free 所需头文件

int main(void)
{
    printf("=== 片段1：堆内存的申请与释放（malloc / free）===\n\n");

    /*
     * malloc 函数：向 C 运行库的分配器申请一块动态存储
     *   参数：需要分配的字节数
     *   返回值：指向分配内存的指针（void * 类型）
     *
     * 示例：申请存放 10 个 int 的空间
     *   sizeof(int) 返回一个 int 占用的字节数（通常为 4）
     *   总字节数写成 10 * sizeof(int)，不假设 int 固定为 4 字节
     */
    int *p = malloc(10 * sizeof(int));  // C 中 void * 会自动转换为对象指针

    if (p == NULL)
    {
        // malloc 可能失败（内存不足），失败时返回 NULL
        printf("内存分配失败！\n");
        return 1;
    }

    // 使用这块内存：像普通数组一样通过下标访问
    for (int i = 0; i < 10; i++)
    {
        p[i] = i * 10;
        printf("p[%d] = %d\n", i, p[i]);
    }

    /*
     * free 函数：释放之前 malloc 申请的内存
     *   一定要在不再使用这块内存时调用 free
     *   忘记 free 会导致"内存泄漏"——内存被占用但不被使用
     */
    free(p);
    // 注意：free 之后不能再使用 p 指向的内存，否则是"悬空指针"问题
    // 好的习惯：free 后将指针设为 NULL
    p = NULL;


    printf("\n\n=== 片段2：字符串字面量具有静态存储期，不可修改 ===\n\n");

    /*
     * 字符串字面量（如 "Hello"）具有静态存储期，具体映射区域由实现决定
     * 使用 const 指针表达“程序不得修改字面量”这一语言规则
     */
    const char *p2 = "Hello";   // p2 指向字符串字面量，只允许读取

    printf("字符串字面量地址：%p\n", (const void *)p2);
    printf("字符串值：%s\n", p2);

    /*
     * 危险操作！试图修改字符串字面量
     * 即使去掉 const 强行写入，行为也未定义，不保证一定以崩溃收场
     *
     *   p2[0] = 'h';  // 危险！试图修改只读内存
     *
     * 为什么危险？
     *   C 语言规定字符串字面量的内容不得由程序修改；
     *   具体平台可能把它放在只读页，也可能表现出其他未定义后果。
     *
     * 如果要可修改的字符串，应该用字符数组：
     *   char arr[] = "Hello";  // arr 是独立的可修改数组
     *   arr[0] = 'h';          // 安全
     */

    return 0;
}

