/* ====================================================================
 *  一、为什么需要构建系统？
 * ====================================================================
 *
 * 手写编译命令的问题（以第13章为例）：
 *   clang src/main.c src/mathutils.c -Iinclude -o program.exe
 *
 *   - 源文件多了（10个、50个），命令越来越长，容易漏掉文件
 *   - 头文件目录不止一个时，-I 参数越写越多
 *   - 特殊编译/链接选项（如 -lm 链接数学库）需要记住
 *   - 多人协作时，每个人都要记住相同的编译命令
 *   - 如果换了编译器，命令格式可能完全不同
 *
 * 解决方案：使用构建系统来管理整个构建过程。
 * 只需要写一个描述文件，告诉构建系统：
 *   - 项目有哪些源文件
 *   - 头文件放在哪里
 *   - 生成什么目标文件
 * 然后一个简单的命令就能完成编译。
 */

/* ====================================================================
 *  二、CMake 是什么？Ninja 是什么？
 * ====================================================================
 *
 * CMake：
 *   - "构建系统生成器"，不直接编译代码
 *   - 像"翻译官"一样，把 CMakeLists.txt 翻译成具体的构建指令
 *   - 跨平台：同一份 CMakeLists.txt 可以在 Windows、Linux、macOS 上使用
 *   - 可以生成给不同底层工具使用的构建文件
 *
 * Ninja：
 *   - 一个专注于速度的底层构建工具
 *   - 没有复杂的语法，只负责按照规则文件执行编译命令
 *   - CMake 生成 .ninja 文件后，Ninja 以最少步骤、最快速度完成编译
 *
 * 打比方：
 *   - CMake 是建筑师 → 根据需求画出施工图纸（build.ninja）
 *   - Ninja 是施工队 → 严格按图纸打地基、砌墙、盖屋顶
 *
 * 在 Windows 上，我们：
 *   1. 用 CMake 生成 Ninja 的构建文件
 *   2. 由 Ninja 调用 Clang 编译器完成真正的编译
 *   整个过程只需要两条命令。
 */

/* ====================================================================
 *  三、CMakeLists.txt 核心命令详解
 * ====================================================================
 *
 * 以一个典型的多文件项目为例：
 *
 * ///////// CMakeLists.txt 示例 /////////
 *
 * cmake_minimum_required(VERSION 3.16)    ← 声明 CMake 最低版本
 * project(MyProject LANGUAGES C)          ← 项目名 + 编程语言
 *
 * add_executable(myapp                     ← 定义可执行文件目标
 *     src/main.c                           ← 源文件列表
 *     src/calc.c
 * )
 *
 * target_include_directories(myapp         ← 给目标添加头文件搜索路径
 *     PRIVATE include                      ← PRIVATE: 仅对 myapp 可见
 * )
 *
 * ////////////////////////////////////////
 *
 * 命令详解：
 *
 * cmake_minimum_required(VERSION 3.16)
 *   声明项目至少需要 CMake 3.16 版本。如果用户的 CMake 版本太低，
 *   CMake 会给出明确的错误提示。
 *
 * project(项目名 LANGUAGES C)
 *   定义项目名称，并指定使用的编程语言。
 *   LANGUAGES C 告诉 CMake 启用 C 编译器检测。
 *
 * add_executable(目标名 源文件1 源文件2 ...)
 *   告诉 CMake："把这些源文件编译链接成一个可执行文件"。
 *   目标名在 Windows 上会自动加 .exe 后缀。
 *
 * target_include_directories(目标名 PRIVATE 路径)
 *   给指定目标添加头文件搜索路径。
 *   PRIVATE 表示这个路径只对当前目标可见。
 *   等价于手写命令中的 clang ... -Iinclude
 */

/* ====================================================================
 *  四、配置、生成、编译三步走
 * ====================================================================
 *
 * 第一步：配置与生成
 *   cmake -S . -B build -G Ninja
 *
 *   参数说明：
 *     -S .         源文件目录（Source），. 表示当前目录
 *     -B build     构建目录（Build），所有中间文件和产物都放这里
 *                  【好处】源码和构建产物分离，保持项目目录干净
 *     -G Ninja     生成器（Generator），指定为 Ninja
 *                  CMake 会在 build/ 下生成 build.ninja
 *
 *   这一步会：
 *     - 检测系统中安装的编译器（Clang / GCC / MSVC）
 *     - 检查编译器是否可用
 *     - 在 build/ 目录生成 build.ninja 和辅助文件
 *
 *   只需要执行一次，或者在修改 CMakeLists.txt 之后重新执行。
 *
 * 第二步：编译
 *   cmake --build build
 *
 *   这条命令会调用 Ninja，按照 build.ninja 中的规则执行编译。
 *   每次修改了 .c 源文件后，只需要重复这一步即可。
 *
 *   如果加了新文件或改了 CMakeLists.txt，才需要重新执行第一步。
 *
 * 第三步：运行
 *   build\myapp.exe
 *
 *   生成的可执行文件位于 build/ 目录中。
 */

/* ====================================================================
 *  五、与传统手写命令的对比
 * ====================================================================
 *
 * 传统方式（手写 clang 命令）：
 *   clang src/main.c src/calc.c -Iinclude -o myapp.exe
 *
 *   - 每次编译都要输入完整命令（或按上箭头回显）
 *   - 添加新源文件时需要手动更新命令
 *   - 其他人拿到代码不知道用什么命令编译
 *
 * CMake 方式：
 *   第一次：cmake -S . -B build -G Ninja
 *   之后：  cmake --build build
 *
 *   - 构建指令被清晰地记录在 CMakeLists.txt 中
 *   - 任何人拿到项目都能用两条命令编译
 *   - 不需要记住复杂的 -I 和文件列表
 *   - 跨平台：同一份 CMakeLists.txt 在 Windows/Linux/macOS 都能用
 */

/* ====================================================================
 *  六、CMake 还能做什么（进阶功能简介）
 * ====================================================================
 *
 * 本章只介绍了最核心的功能，CMake 还能：
 *   - 制作库文件
 *     将自己写的模块编译成静态库（.lib/.a）或动态库（.dll/.so），
 *     方便被其他项目引用。
 *
 *   - 引入第三方库
 *     通过 find_package 轻松使用外部成熟库（如图形库、网络库），
 *     无需手动配置头文件路径和链接选项。
 *
 *   - 条件编译
 *     根据操作系统（Windows/Linux/macOS）、编译器类型、
 *     用户选项决定编译哪些代码。
 *
 *   - 自动测试
 *     集成 CTest，一键运行测试用例。
 */

/* ====================================================================
 *  七、CMakeLists.txt 核心命令速查表
 * ====================================================================
 *
 * ┌──────────────────────────────────────────────────────────────┐
 * │ 命令                             │ 作用                     │
 * ├──────────────────────────────────────────────────────────────┤
 * │ cmake_minimum_required(VERSION x)│ 指定 CMake 最低版本要求  │
 * │ project(名称 LANGUAGES C)        │ 定义项目名称和语言       │
 * │ add_executable(目标 源文件...)   │ 生成可执行文件           │
 * │ target_include_directories(...)  │ 添加头文件搜索路径       │
 * └──────────────────────────────────────────────────────────────┘
 *
 * 命令行速查：
 *   配置与生成： cmake -S . -B build -G Ninja
 *   编译：       cmake --build build
 *
 * 核心概念总结：
 *   1. CMake 是构建系统生成器
 *   2. Ninja 是底层构建工具
 *   3. 源码和构建产物分离（-S 和 -B）
 *   4. 新增头文件路径写在 CMakeLists.txt 而非命令行中
 */

