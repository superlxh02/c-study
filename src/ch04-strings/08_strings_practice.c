#include <stdio.h>
#include <string.h>  // strlen 和 strcmp 函数需要此头文件

int main(void)
{
    // ============================================================
    // 练习 1：反转字符串（首尾交换）
    // 目标：将 "abcdef" 反转为 "fedcba"
    // 算法：从两端向中间，逐对交换字符
    // ============================================================
    char str[] = "abcdef";  // 7 个字节（6 个字符 + '\0'）
    size_t len = strlen(str);  // len = 6（不含 '\0'）

    printf("原字符串：%s\n", str);

    // 首尾交换：只需要循环 len/2 次
    // 第 1 次交换：str[0] ↔ str[5]
    // 第 2 次交换：str[1] ↔ str[4]
    // 第 3 次交换：str[2] ↔ str[3]
    // 循环 3 次后停止，如果 len 是奇数则中间元素不需交换
    for (size_t i = 0; i < len / 2; i++)
    {
        // 三变量法交换两个字符
        char temp = str[i];              // 暂存左端字符
        str[i] = str[len - 1 - i];      // 右端字符放到左端
        str[len - 1 - i] = temp;        // 暂存的左端字符放到右端
    }

    printf("反转后：  %s\n\n", str);

    // ============================================================
    // 练习 2：统计字符串中字母、数字和其他字符的个数
    // 目标：分类统计 "Hello 123 World!" 中各类字符的数量
    // ============================================================
    char text[] = "Hello 123 World!";
    int letters = 0;  // 字母计数器
    int digits = 0;   // 数字计数器
    int others = 0;   // 其他字符计数器（空格、标点等）

    // 遍历字符串，条件 text[i] != '\0' 表示"不是结束符就继续"
    // 这是遍历字符串的标准写法（利用 '\0' 作为终止条件）
    for (int i = 0; text[i] != '\0'; i++)
    {
        // 判断是否为字母（大写 A~Z 或小写 a~z）
        if ((text[i] >= 'A' && text[i] <= 'Z') ||
            (text[i] >= 'a' && text[i] <= 'z'))
        {
            letters++;  // 字母计数 +1
        }
        // 判断是否为数字字符（'0' ~ '9'）
        else if (text[i] >= '0' && text[i] <= '9')
        {
            digits++;   // 数字计数 +1
        }
        else
        {
            others++;   // 既不是字母也不是数字，计入"其他"
        }
    }

    printf("字符串 \"%s\" 中：\n", text);
    printf("  字母：%d 个\n", letters);
    printf("  数字：%d 个\n", digits);
    printf("  其他：%d 个\n\n", others);

    // ============================================================
    // 练习 3：比较用户输入的两个字符串
    // 目标：读入两个单词，用 strcmp 比较它们的字典序
    // ============================================================
    char word1[20], word2[20];  // 两个字符数组分别存储两个单词

    // 读取第一个单词（用 scanf + %s，适合读单个无空格单词）
    printf("请输入第一个单词：");
    if (scanf("%19s", word1) != 1)
    {
        fprintf(stderr, "没有读到第一个单词。\n");
        return 1;
    }

    // 读取第二个单词
    printf("请输入第二个单词：");
    if (scanf("%19s", word2) != 1)
    {
        fprintf(stderr, "没有读到第二个单词。\n");
        return 1;
    }

    // 使用 strcmp 比较两个字符串的字典序
    int result = strcmp(word1, word2);

    if (result == 0)  // 返回 0 表示两者完全相同
    {
        printf("两个单词相同\n");
    }
    else if (result < 0)  // 返回负数：word1 在字典中排在 word2 前面
    {
        printf("\"%s\" 在 \"%s\" 的前面\n", word1, word2);
    }
    else  // 返回正数：word1 在字典中排在 word2 后面
    {
        printf("\"%s\" 在 \"%s\" 的后面\n", word1, word2);
    }

    return 0;
}

/*
 * ============================================================
 * 预期输出：
 * ------------------------------------------------------------
 * 原字符串：abcdef
 * 反转后：  fedcba
 *
 * 字符串 "Hello 123 World!" 中：
 *   字母：10 个
 *   数字：3 个
 *   其他：3 个
 *
 * 请输入第一个单词：apple
 * 请输入第二个单词：banana
 * "apple" 在 "banana" 的前面
 * ============================================================
 *
 * 各练习要点：
 *
 * 练习 1（反转字符串）：
 * - 循环次数：len / 2（只需交换一半，索引从 0 到 len/2 - 1）
 * - 对称位置：str[i] ↔ str[len - 1 - i]
 * - 使用临时变量 temp 完成交换
 * - '\0' 始终在末尾不动，不需要交换
 *
 * 练习 2（分类统计字符）：
 * - 字符判断使用 ASCII 码范围：'A'~'Z' (65~90)、'a'~'z' (97~122)、'0'~'9' (48~57)
 * - 遍历条件 text[i] != '\0' 是处理字符串的标准循环写法
 * - 分类覆盖所有情况：if...else if...else 确保不漏掉任何字符
 * - 本例中空格和感叹号属于"其他"类
 *
 * 练习 3（比较用户输入）：
 * - 单次无空格字符串输入可用 scanf + 带宽度的 %s，例如 "%19s"
 * - strcmp 返回值：0=相等，负=前者小，正=前者大
 * - 字典序比较基于 ASCII 码：'a'(97) < 'b'(98)
 */
