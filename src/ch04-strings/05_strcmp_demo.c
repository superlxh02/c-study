#include <stdio.h>
#include <string.h>  // strcmp 函数声明在此头文件中

int main(void)
{
    // 声明并初始化两个字符串
    char str1[] = "apple";
    char str2[] = "banana";

    // ----- 使用 strcmp 比较两个字符串的内容 -----
    // strcmp(str1, str2) 的返回值含义：
    //   返回 0   → str1 和 str2 内容完全相同
    //   返回负数  → str1 按字典序排在 str2 前面（str1 < str2）
    //   返回正数  → str1 按字典序排在 str2 后面（str1 > str2）

    if (strcmp(str1, str2) == 0)  // 返回值为 0 表示相等
    {
        printf("两个字符串相等\n");
    }
    else
    {
        printf("两个字符串不相等\n");
    }

    return 0;
}

/*
 * ============================================================
 * 预期输出：
 * ------------------------------------------------------------
 * 两个字符串不相等
 * （因为 "apple" ≠ "banana"）
 * ============================================================
 *
 * 为什么不能用 == 比较字符串？
 *
 * 错误写法：
 *   if (str1 == str2)  // 这比较的是地址，不是内容！
 *
 * 正确写法：
 *   if (strcmp(str1, str2) == 0)  // 这比较的是内容
 *
 * 原理解释：
 * 数组名 str1 和 str2 代表的是各自数组在内存中的起始地址。
 * 即使两个数组的内容一模一样，它们也存储在不同的内存位置。
 * str1 == str2 比较的是"这两个数组是否在同一块内存区域"——
 * 答案永远是否定的，即使内容相同也会返回假。
 *
 * 类比：两个不同地址的房子，即使里面的家具（字符）一模一样，
 * 门牌号（地址）也是不同的。必须逐一比较家具，才能判断内容相等。
 * strcmp 做的正是逐字符比较的工作。
 *
 * strcmp 的返回值规则：
 * | 比较结果          | 返回值 | 示例                          |
 * |------------------|--------|------------------------------|
 * | str1 等于 str2    | 0      | strcmp("abc", "abc") == 0    |
 * | str1 小于 str2    | 负数   | strcmp("abc", "abd") < 0     |
 * | str1 大于 str2    | 正数   | strcmp("abd", "abc") > 0     |
 *
 * 字典序比较原理：
 * strcmp 从第一个字符开始逐个比较 ASCII 码值。
 * "apple" vs "banana"：第一个字符 'a'(97) vs 'b'(98)，97 < 98，
 * 所以 "apple" < "banana"，strcmp 返回负数。
 */
