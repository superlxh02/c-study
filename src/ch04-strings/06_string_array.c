#include <stdio.h>

int main(void)
{
    // 声明一个二维字符数组，存储 3 个字符串，每个最多 20 个字符
    // 3 行 × 20 列：3 个字符串，每个字符串最多 19 个可见字符 + '\0'
    // fruits[0] 是第一个字符串，fruits[1] 是第二个字符串...
    char fruits[3][20] = {
        "apple",   // 第 0 行：第一个水果名称
        "banana",  // 第 1 行：第二个水果名称
        "orange"   // 第 2 行：第三个水果名称
    };

    // ----- 遍历字符串数组 -----
    // 外层循环遍历每一行（每一个字符串）
    // fruits[i] 可以当作一个独立的一维字符数组来用
    for (int i = 0; i < 3; i++)
    {
        // 用 %s 输出每一行的字符串
        // fruits[i] 是第 i 行的起始地址，printf 从该地址开始输出
        // 直到遇到该行末尾的 '\0' 为止
        printf("水果 %d：%s\n", i + 1, fruits[i]);
    }

    return 0;
}

/*
 * ============================================================
 * 预期输出：
 * ------------------------------------------------------------
 * 水果 1：apple
 * 水果 2：banana
 * 水果 3：orange
 * ============================================================
 *
 * 内存布局示意图（3 行 × 20 列）：
 *
 * 行 0: ['a']['p']['p']['l']['e']['\0'][ ][ ][ ][ ][ ][ ][ ][ ]...  (19个空位)
 * 行 1: ['b']['a']['n']['a']['n']['a']['\0'][ ][ ][ ][ ][ ][ ][ ]...
 * 行 2: ['o']['r']['a']['n']['g']['e']['\0'][ ][ ][ ][ ][ ][ ][ ]...
 *
 * 理解方式：
 * 1. fruits 是一个二维数组：3 行，20 列
 * 2. 每一行就是一个独立的字符串（字符数组），以 '\0' 结尾
 * 3. fruits[i] 代表第 i 行的起始地址，可以当作一维字符数组来使用
 * 4. fruits[i][j] 访问第 i 个字符串的第 j 个字符
 *    例如：fruits[1][0] = 'b'（"banana" 的第 1 个字符）
 *
 * 用途：
 * 字符串数组适合存储菜单、选项列表、分类标签等"一组字符串"的数据。
 */
