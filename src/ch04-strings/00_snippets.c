#include <stdio.h>
#include <string.h>

// ============================================================
// 片段1：字符串的本质 —— 以 '\0' 结尾的字符数组
// ============================================================

// 方式一：手动逐个字符初始化（麻烦，不推荐手写 '\0'）
/*
char str[6] = {'H', 'e', 'l', 'l', 'o', '\0'};  // 6个元素，最后一个是 '\0'
*/
// 内存视图：[H][e][l][l][o][\0]
// 数组大小 = 可见字符数 + 1（为 '\0' 留空间）

// 方式二：用双引号创建（推荐 —— 编译器自动添加 '\0'）
/*
char str[] = "Hello";  // 编译器自动在末尾添加 '\0'
*/
// 效果与方式一完全相同，但写法更简洁、更不容易遗漏 '\0'
// 实际分配了 6 个字节（5 个可见字符 + 1 个 '\0'）

// 方式三：声明时指定大小，用双引号初始化
/*
char str[20] = "Hello";  // 预留 20 字节空间，前 6 字节为 "Hello\0"
*/

// ============================================================
// 片段2：单引号与双引号的本质区别
// ============================================================
/*
char c = 'A';        // 字符：用单引号包裹，只包含字符 A 本身
char str[] = "A";    // 字符串：用双引号包裹，包含 'A' 和 '\0'

// 'A' 占 1 个字节（只是一个 char 值）
// "A" 占 2 个字节（'A' + '\0'）

// 以下写法是错误的：
// char c = "A";     // 错误！"A" 是字符串（字符数组），不能赋值给单个 char 变量
// char str[] = 'A'; // 错误！'A' 是单个字符，不是字符串

// 正确写法：
char c = 'A';        // 正确：单个字符赋给 char 变量
char str[] = "A";    // 正确：字符串赋给字符数组
*/

// ============================================================
// 片段3：strcpy —— 复制字符串（危险：不检查目标容量）
// ============================================================
/*
// 使用 strcpy 前需要 #include <string.h>

char dest[20];             // 目标数组，必须有足够空间
char src[] = "Hello, World!";

strcpy(dest, src);  // 把 src 的内容（包括 '\0'）复制到 dest 中
// 执行后 dest 的内容也为 "Hello, World!"

// 危险提示：
// strcpy 不会检查目标数组的大小！
// 如果 dest 只有 10 个字节而 src 有 20 个字符，
// 多余的字符会覆盖 dest 后面的内存，造成缓冲区溢出。
// 实际开发中应使用带长度限制的函数，或确保目标空间足够。
*/

// ============================================================
// 片段4：strcat —— 拼接字符串（必须有足够空间容纳合并结果）
// ============================================================
/*
// 使用 strcat 前需要 #include <string.h>

char str1[30] = "Hello";   // 必须足够大，能容纳合并后的全部内容
char str2[] = " World";

strcat(str1, str2);  // str1 变成 "Hello World"

// 规则：
// 1. str1 必须有足够的空间容纳：strlen(str1) + strlen(str2) + 1（'\0'）
// 2. strcat 从 str1 原有的 '\0' 位置开始，把 str2 的内容拷贝过去
// 3. str2 本身不会被修改

// 注意：如果 str1 容量不够，同样会造成缓冲区溢出！
*/

// ============================================================
// 片段5：scanf 读取字符串的两个注意事项
// ============================================================
/*
char name[20];
scanf("%19s", name);  // 注意 1：name 前面不加 &，最多读 19 个可见字符

// 注意 2：%s 遇到空格就停止读取
// 如果用户输入 "Xiao Ming"，name 只会存入 "Xiao"，"Ming" 留在缓冲区
// 这意味着 scanf + %s 无法读取包含空格的字符串（如全名）

// 注意 3：如果写成 scanf("%s", name)，且用户输入超过 19 个字符，
// scanf 不会阻止，会继续往内存写——缓冲区溢出！
// 因此推荐使用 fgets 替代 scanf 读取字符串
*/

// ============================================================
// 片段6：为什么不能用 == 比较两个字符串
// ============================================================
/*
char str1[] = "apple";
char str2[] = "apple";

// 错误写法：
// if (str1 == str2)  ← 比较的是两个数组的地址，而不是内容！

// 正确写法：
if (strcmp(str1, str2) == 0)
{
    // 字符串内容相等
}

// 原理：数组名 str1 和 str2 代表的是各自数组在内存中的起始地址。
// str1 == str2 是在问"这两个数组是否在同一个位置"——答案永远是否定的。
// 就像两个不同地址的房子，即使内部装饰一模一样，门牌号也不同。
// 必须用 strcmp 逐字符比较，才能判断内容是否相等。
*/
