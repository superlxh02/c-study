#include <stdio.h>

int main(void)
{
    // 声明一个能存放 20 个字符的数组
    char name[20];

    // 提示用户输入全名
    printf("请输入你的全名：");

    // fgets 的三个参数：
    //   参数1：字符数组名    —— 存储输入内容的数组
    //   参数2：最大读取字节数 —— 包括最后的 '\0'（本例最多读 19 个字符）
    //   参数3：输入来源      —— stdin 表示标准输入（键盘）
    if (fgets(name, sizeof name, stdin) == NULL)
    {
        fprintf(stderr, "读取输入失败\n");
        return 1;
    }

    // 输出读入的字符串
    // 注意：fgets 会把用户按下的回车符 '\n' 也存入数组，
    // 所以输出后会自动换行（多一个空行）
    printf("你好，%s\n", name);

    return 0;
}

/*
 * ============================================================
 * 运行示例（输入 "Xiao Ming"）：
 * ------------------------------------------------------------
 * 请输入你的全名：Xiao Ming
 * 你好，Xiao Ming
 * （注意：此处 name 末尾其实还包含一个 '\n'，所以输出后会多空一行）
 * ============================================================
 *
 * fgets 与 scanf + %s 的对比：
 *
 * | 特性            | scanf("%s", name) | fgets(name, 20, stdin) |
 * |----------------|-------------------|------------------------|
 * | 读取含空格的字符串 | 不行（遇到空格停）   | 可以                    |
 * | 防止缓冲区溢出    | 不会阻止           | 会限制最多读 N-1 个字符   |
 * | 保留换行符       | 不保留              | 会保留回车符 '\n'        |
 * | 参数中写 &       | 不加 &              | 不加 &                  |
 *
 * fgets 工作原理：
 * 1. 从 stdin 读取字符，直到满足以下任一条件：
 *    - 读到了换行符 '\n'（保留换行符在数组中）
 *    - 已读取了 (n-1) 个字符（留一个位置给 '\0'）
 *    - 遇到了文件结束符 EOF
 * 2. 在所有读取的字符之后自动添加 '\0' 作为结束标志
 *
 * 小缺陷：fgets 会保留末尾的换行符 '\n'
 * 如果不需要换行符，可以用 07_remove_newline.c 中的方法去除。
 */
