#include <stdio.h>

int main(void)
{
    // 声明并初始化一个字符串（字符数组）
    // 编译器会在 "Xiao Ming" 末尾自动添加 '\0'
    // 实际占用内存：9 个可见字符 + 1 个 '\0' = 10 字节
    char name[] = "Xiao Ming";

    // ----- 使用 %s 占位符输出字符串变量 -----
    // %s 告诉 printf：从 name 的起始地址开始，逐个输出字符，
    // 直到遇到 '\0'（字符串结束标志）才停止
    printf("你好，%s！\n", name);  // 预期输出：你好，Xiao Ming！

    // ----- 直接输出字符串常量（双引号包裹的字面量） -----
    // 字符串常量本身就是以 '\0' 结尾的，所以不需要变量
    printf("欢迎学习C语言\n");

    return 0;
}

/*
 * ============================================================
 * 预期输出：
 * ------------------------------------------------------------
 * 你好，Xiao Ming！
 * 欢迎学习C语言
 * ============================================================
 *
 * printf 输出字符串的内部机制：
 * 1. printf 从给定的内存地址开始，逐个字节读取并输出
 * 2. 每读到一个字符就输出一个字符
 * 3. 当读到 '\0'（ASCII 码为 0）时停止输出
 * 4. 如果字符数组末尾忘了放 '\0'，printf 会一直读取并输出，
 *    直到在内存中碰巧遇到一个 '\0' 为止 → 输出乱码！
 *
 * 要点总结：
 * 1. %s 格式占位符专门用于输出字符串
 * 2. 可以输出字符串变量（name），也可以输出字符串常量（"..."）
 * 3. 字符串必须以 '\0' 结尾，否则 printf 会越界输出垃圾数据
 * 4. name 是数组名，代表数组的起始地址，printf 从该地址开始读取
 */
