#include <stdio.h>

// 定义 Student 结构体类型
struct Student
{
    char name[20];    // 姓名
    int age;          // 年龄
    float score;      // 成绩
};

int main(void)
{
    /*
     * 结构体数组：一个数组，其中的每个元素都是一个完整的结构体变量
     * 非常适合管理多条相同结构的记录（如一个班的所有学生）
     *
     * 初始化语法：用 { } 包裹整个数组，每个元素再用 { } 包裹
     */
    struct Student students[3] = {
        {"小明", 18, 92.5},   // students[0]
        {"小红", 19, 88.0},   // students[1]
        {"小刚", 18, 95.5}    // students[2]
    };

    // 用 for 循环遍历结构体数组
    // 访问方式：students[i] 是第 i+1 个结构体，students[i].name 是其 name 成员
    for (int i = 0; i < 3; i++)
    {
        printf("学生 %d：%s，年龄 %d，成绩 %.1f\n",
               i + 1,
               students[i].name,    // 先取数组元素（结构体），再取成员
               students[i].age,
               students[i].score);
    }

    return 0;
}

/*
 * =====================================================
 * 访问方式分析:
 *   students          → 数组名，代表整个数组
 *   students[i]       → 数组的第 i 个元素，是一个完整的结构体变量
 *   students[i].name  → 该结构体的 name 成员
 *
 * 比喻：
 *   students 是一个储物柜，
 *   每个抽屉（students[0], students[1], ...）里放了一个学生信息包裹，
 *   点运算符打开包裹拿出里面的具体东西。
 *
 * =====================================================
 * 预期输出:
 * =====================================================
 * 学生 1：小明，年龄 18，成绩 92.5
 * 学生 2：小红，年龄 19，成绩 88.0
 * 学生 3：小刚，年龄 18，成绩 95.5
 * =====================================================
 */
