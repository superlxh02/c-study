#include <stdio.h>
#include <string.h>

// ============================================================
// 场景引入：没有结构体时，数据是分散管理的
// ============================================================

/*
 * 假设我们要管理学生的姓名、成绩、年龄这三类信息。
 * 如果只用数组，就需要三个独立的数组来分别存储：
 *   - scores[5]  存储 5 个学生的成绩
 *   - names[5][20] 存储 5 个学生的名字
 *   - ages[5]    存储 5 个学生的年龄
 *
 * 问题：同一个学生的三样信息分散在三个数组里，通过同一个下标(index)
 * 来关联。这意味着：
 *   1. 传递一个学生的完整信息需要三个数组 + 一个下标
 *   2. 如果下标不一致，数据就对应错了
 *   3. 无法把"一个学生"作为一个整体来处理
 */

// 不用结构体时，打印某个学生的信息需要传递三个数组和下标
// 参数多、容易出错、不直观
void printStudent(char names[][20], int scores[], int ages[], int index)
{
    printf("姓名：%s，成绩：%d，年龄：%d\n",
           names[index], scores[index], ages[index]);
}

int main(void)
{
    // ------ 不使用结构体的做法：分散数组 ------
    int scores[5] = {85, 92, 78, 90, 88};        // 5 个学生的成绩
    char names[5][20] = {"小明", "小红", "小刚", "小丽", "小华"}; // 5 个学生的名字
    int ages[5] = {18, 19, 18, 20, 19};          // 5 个学生的年龄

    printf("=== 不使用结构体的分散数组方式 ===\n");
    // 想打印小明的全部信息，需要记住三个数组里他都对应下标 0
    printf("小明 -> 姓名：%s，成绩：%d，年龄：%d\n",
           names[0], scores[0], ages[0]);

    // 用函数打印也需要三个数组参数
    printf("\n用函数打印第 2 个学生（小红）：\n");
    printStudent(names, scores, ages, 1);

    // ------ 使用结构体的做法：数据打包在一起 ------

    printf("\n\n=== 使用结构体打包数据 ===\n");

    /*
     * 第一步：定义结构体类型（设计"模板"）
     * struct 是关键字，Student 是我们给这个新类型起的名字
     * 花括号内是成员列表，每个成员以分号结尾
     * 注意：右花括号后面的分号 ; 绝对不能忘记！
     */
    struct Student
    {
        char name[20];   // 姓名 —— 字符数组
        int age;         // 年龄 —— 整数
        float score;     // 成绩 —— 浮点数
    };  // <-- 这个分号很重要！新手常犯的错误就是漏掉它

    /*
     * 第二步：声明结构体变量（根据模板创建具体实例）
     * 声明时必须带 struct 关键字
     * stu1 和 stu2 各自拥有 name、age、score 三个成员
     */
    struct Student stu1;  // stu1 是一个 Student 类型的变量
    struct Student stu2;  // stu2 是另一个 Student 类型的变量

    // 本节只演示 stu2 的声明；显式转为空操作，表明它并非遗漏使用。
    (void)stu2;

    // 给 stu1 赋值（在声明后，通过 strcpy 给字符串成员赋值）
    strcpy(stu1.name, "小明");
    stu1.age = 18;
    stu1.score = 92.5;

    // 注意：下面这行是错误示范，字符数组不能直接用 = 赋值
    // stu2.name = "小红";  // 编译错误！字符数组不是左值

    // 小结：结构体让我们可以把一个学生的所有信息打包在一起，
    // 作为一个整体来操作。这比用三个分离的数组直观得多。

    printf("stu1: 姓名=%s, 年龄=%d, 成绩=%.1f\n",
           stu1.name, stu1.age, stu1.score);

    return 0;
}

