#include <stdio.h>

/*
 * 定义 Student 结构体类型（设计模板）
 * 包含三个成员：姓名（字符串）、年龄（整数）、成绩（浮点数）
 */
struct Student
{
    char name[20];    // 姓名缓冲区：最多 19 字节内容，另留 1 字节给 '\0'
    int age;          // 年龄
    float score;      // 成绩
};

int main(void)
{
    /*
     * 方式一：声明的同时用花括号 {} 初始化
     * 花括号中的值必须按照结构体成员定义的顺序依次给出
     * 对于字符串成员，声明时初始化可以直接写字符串字面量
     */
    struct Student stu1 = {"小明", 18, 92.5};

    // 方式二：先声明，再逐个赋值
    // 注意：声明之后再想给字符串成员赋值，就不能直接用 = 了
    // stu2.name = "小红";  // 编译错误！字符数组不能这样赋值
    // 正确做法是用 strcpy 函数（将在后面的小节学习）
    struct Student stu2;

    // 本小节尚未讲解 strcpy，因此只声明 stu2，并明确暂不读取未初始化成员。
    (void)stu2;

    // 输出结构体 stu1 的各个成员
    // 使用点运算符 . 来访问：变量名.成员名
    printf("姓名：%s\n", stu1.name);
    printf("年龄：%d\n", stu1.age);
    printf("成绩：%.1f\n", stu1.score);   // %.1f 保留一位小数

    return 0;
}

/*
 * =====================================================
 * 预期输出:
 * =====================================================
 * 姓名：小明
 * 年龄：18
 * 成绩：92.5
 * =====================================================
 */
