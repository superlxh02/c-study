/**
 * =====================================================
 * 文件名: 07_nested_struct.c
 * 所属章节: 第五章
 * 对应小节: 二-4. 结构体的嵌套——结构体里放结构体
 * 演示内容: 一个结构体的成员可以是另一个结构体；
 *          初始化嵌套结构体时用嵌套花括号；
 *          访问嵌套成员时使用多级点运算符
 * =====================================================
 */

#include <stdio.h>

// 日期结构体 —— 先定义"被嵌套"的结构体
struct Date
{
    int year;    // 年
    int month;   // 月
    int day;     // 日
};

/*
 * 学生结构体 —— 包含一个 Date 类型的成员
 * 注意：Date 结构体必须定义在 Student 之前，
 * 否则编译器不知道 struct Date 是什么
 */
struct Student
{
    char name[20];           // 姓名
    int age;                 // 年龄
    float score;             // 成绩
    struct Date birthday;    // 出生日期 —— 嵌套的结构体成员
};

int main(void)
{
    /*
     * 嵌套结构体的初始化：用嵌套的花括号 {}
     * 外层花括号对应 Student 的成员，
     * 内层花括号 {2008, 3, 15} 对应 birthday 这个 Date 结构体
     */
    struct Student stu1 = {"小明", 18, 92.5, {2008, 3, 15}};

    // 输出基本信息
    printf("姓名：%s\n", stu1.name);

    /*
     * 多级点运算符访问嵌套成员：
     *   stu1.birthday       → 取出 birthday 成员（它是一个 struct Date）
     *   stu1.birthday.year  → 再从 Date 结构体中取出 year 成员
     *
     * 比喻：打开一个大包裹，发现里面还有一个小包裹，需要再打开一次。
     * 不管嵌套多少层，每一层都用点运算符 . 来深入。
     */
    printf("出生日期：%d 年 %d 月 %d 日\n",
           stu1.birthday.year,    // 多级点运算符：一层层深入
           stu1.birthday.month,
           stu1.birthday.day);

    return 0;
}

/*
 * =====================================================
 * 多级访问分析：
 *   stu1                 → struct Student 类型变量
 *   stu1.birthday        → struct Date 类型变量（一个完整结构体）
 *   stu1.birthday.year   → int 类型，年份值
 *   stu1.birthday.month  → int 类型，月份值
 *   stu1.birthday.day    → int 类型，日期值
 *
 * 常见误区：
 *   - 点运算符的级数可以无限延伸，理论上可以一直嵌套下去
 *   - 结构体不能嵌套自身（会造成无限递归大小），但可以包含指向自身的指针
 *
 * =====================================================
 * 预期输出:
 * =====================================================
 * 姓名：小明
 * 出生日期：2008 年 3 月 15 日
 * =====================================================
 */
